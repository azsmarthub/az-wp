                    log_success "Telegram alerts configured! Confirmation sent to your Telegram."
                    ;;
                telegram-test)
                    local tg_token tg_chat
                    tg_token="$(config_get TG_BOT_TOKEN 2>/dev/null)" || tg_token=""
                    tg_chat="$(config_get TG_CHAT_ID 2>/dev/null)" || tg_chat=""
                    if [[ -z "$tg_token" || -z "$tg_chat" ]]; then
                        log_warn "Telegram not configured. Run: azwp advanced security telegram-setup"
                        return 0
                    fi
                    local test_msg="✅ *azwp Test Alert*
🌐 \`${DOMAIN}\`
⏰ $(date '+%Y-%m-%d %H:%M:%S')
This is a test message from azwp security scanner."
                    if curl -sf -X POST "https://api.telegram.org/bot${tg_token}/sendMessage" \
                        -d "chat_id=${tg_chat}" \
                        -d "text=${test_msg}" \
                        -d "parse_mode=Markdown" > /dev/null 2>&1; then
                        log_success "Test message sent! Check your Telegram."
                    else
                        log_error "Failed to send. Check Token and Chat ID."
                    fi
                    ;;
                f2b-status) fail2ban-client status 2>/dev/null; for jail in $(fail2ban-client status 2>/dev/null | grep "Jail list" | sed 's/.*://;s/,/ /g'); do printf "\n"; fail2ban-client status "$jail" 2>/dev/null; done ;;
                unban) local ip; read -rp "  IP to unban: " ip; [[ -n "$ip" ]] && fail2ban-client unban "$ip" 2>/dev/null && log_success "Unbanned $ip" ;;
                ufw) ufw status verbose 2>/dev/null ;;
            esac
            ;;

        # --- Performance ---
        perf)
            local perf_sub="${extra}"
            if [[ -z "$perf_sub" ]]; then
                _header "Performance"
                printf "  1) Re-tune (after VPS resize)\n"
                printf "  2) Test TTFB\n"
                printf "  3) FPM status\n"
                printf "  4) OPcache status\n"
                printf "  5) Reload PHP-FPM\n"
                printf "  0) Back\n\n"
                read -rp "  Choose: " perf_sub
                case "$perf_sub" in 1) perf_sub="retune" ;; 2) perf_sub="ttfb" ;; 3) perf_sub="fpm" ;; 4) perf_sub="opcache" ;; 5) perf_sub="reload-fpm" ;; *) return 0 ;; esac
            fi
            case "$perf_sub" in
                retune)
                    detect_hardware; calculate_tune; print_tune_summary
                    confirm "Apply new tuning?" || return 0
                    # Re-render configs
                    configure_nginx; configure_fastcgi_cache; configure_site
                    configure_php_ini; configure_opcache; configure_fpm_pools
                    configure_mariadb; configure_redis
                    systemctl restart nginx "php${PHP_VERSION}-fpm" mariadb redis-server
                    log_success "All configs re-tuned and services restarted."
                    ;;
                ttfb)
                    printf "  Testing TTFB for https://%s ...\n" "$DOMAIN"
                    curl -sk --resolve "${DOMAIN}:443:127.0.0.1" -o /dev/null \
                        -w "  TTFB:  %{time_starttransfer}s\n  Total: %{time_total}s\n" \
                        "https://${DOMAIN}/"
                    ;;
                fpm)
                    printf "\n  ${BOLD}Web pool:${NC}\n"
                    curl -sfk --resolve "${DOMAIN}:443:127.0.0.1" "https://${DOMAIN}/fpm-status" 2>/dev/null | sed 's/^/  /' \
                        || curl -sf http://127.0.0.1/fpm-status 2>/dev/null | sed 's/^/  /' \
                        || printf "  (not accessible)\n"
                    printf "\n  ${BOLD}Workers pool:${NC}\n"
                    curl -sfk --resolve "${DOMAIN}:443:127.0.0.1" "https://${DOMAIN}/fpm-workers-status" 2>/dev/null | sed 's/^/  /' \
                        || curl -sf http://127.0.0.1/fpm-workers-status 2>/dev/null | sed 's/^/  /' \
                        || printf "  (not accessible)\n"
                    ;;
                opcache)
                    _header "OPcache Configuration"
                    local opcache_ini="/etc/php/${PHP_VERSION}/fpm/conf.d/10-opcache-az.ini"
                    if [[ -f "$opcache_ini" ]]; then
                        local oc_enabled oc_mem oc_jit oc_files
                        oc_enabled="$(grep 'opcache.enable ' "$opcache_ini" | cut -d= -f2 | tr -d ' ')"
                        oc_mem="$(grep 'opcache.memory_consumption' "$opcache_ini" | cut -d= -f2 | tr -d ' ')"
                        oc_jit="$(grep 'opcache.jit_buffer_size' "$opcache_ini" | cut -d= -f2 | tr -d ' ')"
                        oc_files="$(grep 'opcache.max_accelerated_files' "$opcache_ini" | cut -d= -f2 | tr -d ' ')"
                        if [[ "$oc_enabled" == "1" ]]; then
                            printf "  Status:       ${GREEN}ENABLED${NC}\n"
                        else
                            printf "  Status:       ${RED}DISABLED${NC}\n"
                        fi
                        printf "  Memory:       %s MB\n" "${oc_mem:-0}"
                        printf "  Max files:    %s\n" "${oc_files:-0}"
                        printf "  JIT buffer:   %s\n" "${oc_jit:-0 (disabled)}"
                        printf "  Revalidate:   %ss\n" "$(grep 'revalidate_freq' "$opcache_ini" | cut -d= -f2 | tr -d ' ')"
                    else
                        printf "  OPcache config not found at %s\n" "$opcache_ini"
                    fi
                    printf "\n  ${DIM}Note: OPcache runs inside PHP-FPM only. CLI has it disabled.${NC}\n"
                    printf "  ${DIM}Reset OPcache: azwp advanced perf reload-fpm${NC}\n"
                    ;;
                reload-fpm)
                    service_restart "php${PHP_VERSION}-fpm"
                    log_success "PHP-FPM restarted (OPcache cleared)."
                    ;;
            esac
            ;;

        # --- PHP Info ---
        php)
            local php_sub="${extra}"
            if [[ -z "$php_sub" ]]; then
                _header "PHP ${PHP_VERSION} Info"

                # Version
                local php_ver; php_ver="$(php${PHP_VERSION} -v 2>/dev/null | head -1 | grep -oP 'PHP \K[0-9.]+' || echo '?')"
                printf "  Version:    ${GREEN}PHP %s${NC}\n" "$php_ver"

                # Key config values
                local mem_limit max_exec upload_max
                mem_limit="$(grep 'memory_limit' /etc/php/${PHP_VERSION}/fpm/conf.d/99-azwp.ini 2>/dev/null | cut -d= -f2 | tr -d ' ' || echo '?')"
                max_exec="$(grep 'max_execution_time' /etc/php/${PHP_VERSION}/fpm/conf.d/99-azwp.ini 2>/dev/null | cut -d= -f2 | tr -d ' ' || echo '?')"
                upload_max="$(grep 'upload_max_filesize' /etc/php/${PHP_VERSION}/fpm/conf.d/99-azwp.ini 2>/dev/null | cut -d= -f2 | tr -d ' ' || echo '?')"
                printf "  Memory:     %s\n" "$mem_limit"
                printf "  Max exec:   %ss\n" "$max_exec"
                printf "  Upload max: %s\n" "$upload_max"

                # OPcache
                local oc_mem; oc_mem="$(grep 'memory_consumption' /etc/php/${PHP_VERSION}/fpm/conf.d/10-opcache-az.ini 2>/dev/null | cut -d= -f2 | tr -d ' ' || echo '?')"
                local oc_jit; oc_jit="$(grep 'jit_buffer_size' /etc/php/${PHP_VERSION}/fpm/conf.d/10-opcache-az.ini 2>/dev/null | cut -d= -f2 | tr -d ' ' || echo '0')"
                printf "  OPcache:    %sMB | JIT: %s\n" "$oc_mem" "$oc_jit"

                # FPM pools
                local web_max; web_max="$(grep 'pm.max_children' /etc/php/${PHP_VERSION}/fpm/pool.d/web.conf 2>/dev/null | cut -d= -f2 | tr -d ' ' || echo '?')"
                local wrk_max; wrk_max="$(grep 'pm.max_children' /etc/php/${PHP_VERSION}/fpm/pool.d/workers.conf 2>/dev/null | cut -d= -f2 | tr -d ' ' || echo '?')"
                printf "  FPM:        web=%s | workers=%s\n" "$web_max" "$wrk_max"

                # Extensions
                printf "\n  ${BOLD}Extensions:${NC}\n"
                local ext ext_list
                ext_list="$(php${PHP_VERSION} -m 2>/dev/null | grep -v '^\[' | sort | tr '\n' ' ' || true)"
                local ext_count; ext_count="$(php${PHP_VERSION} -m 2>/dev/null | grep -v '^\[' | grep -v '^$' | wc -l || echo 0)"
                printf "  Total: %s loaded\n\n" "$ext_count"

                # Show key extensions with status
                # Use FPM check for OPcache (CLI has it disabled)
                local key_exts="bcmath curl dom exif fileinfo gd iconv igbinary imagick intl mbstring mysqli pdo_mysql redis soap xml zip"
                for ext in $key_exts; do
                    if php${PHP_VERSION} -m 2>/dev/null | grep -qi "^${ext}$"; then
                        printf "    ${GREEN}%-15s ON${NC}\n" "$ext"
                    else
                        printf "    ${RED}%-15s OFF${NC}\n" "$ext"
                    fi
                done
                # OPcache special check — always ON in FPM if config exists
                if [[ -f "/etc/php/${PHP_VERSION}/fpm/conf.d/10-opcache-az.ini" ]]; then
                    local oc_on; oc_on="$(grep 'opcache.enable ' "/etc/php/${PHP_VERSION}/fpm/conf.d/10-opcache-az.ini" | cut -d= -f2 | tr -d ' ')"
                    if [[ "$oc_on" == "1" ]]; then
                        printf "    ${GREEN}%-15s ON${NC}  ${DIM}(FPM only, CLI disabled by design)${NC}\n" "opcache"
                    else
                        printf "    ${RED}%-15s OFF${NC}\n" "opcache"
                    fi
                else
                    printf "    ${RED}%-15s OFF${NC}  ${DIM}(no config found)${NC}\n" "opcache"
                fi

                printf "\n  1) Install extension\n"
                printf "  2) Show all loaded modules\n"
                printf "  0) Back\n\n"
                read -rp "  Choose: " php_sub
                case "$php_sub" in 1) php_sub="install-ext" ;; 2) php_sub="modules" ;; *) return 0 ;; esac
            fi
            case "$php_sub" in
                install-ext)
                    local ext_name
                    read -rp "  Extension name (e.g. memcached, xdebug): " ext_name
                    [[ -z "$ext_name" ]] && return 0
                    local pkg="php${PHP_VERSION}-${ext_name}"
                    printf "  Installing %s...\n" "$pkg"
                    if NEEDRESTART_MODE=a DEBIAN_FRONTEND=noninteractive apt-get install -y "$pkg" > /dev/null 2>&1; then
                        systemctl restart "php${PHP_VERSION}-fpm"
                        log_success "$pkg installed and FPM restarted."
                    else
                        log_error "Package $pkg not found or install failed."
                        printf "  ${DIM}Search available: apt-cache search php${PHP_VERSION}${NC}\n"
                    fi
                    ;;
                modules)
                    _header "All PHP ${PHP_VERSION} Modules"
                    php${PHP_VERSION} -m 2>/dev/null | grep -v '^\[' | grep -v '^$' | sort | column 2>/dev/null || \
                        php${PHP_VERSION} -m 2>/dev/null | grep -v '^\[' | grep -v '^$' | sort
                    ;;
            esac
            ;;

        # --- Cloudflare Cache ---
        cloudflare)
            _header "Cloudflare Cache Setup"

            local cf_token cf_zone
            cf_token="$(config_get CF_API_TOKEN 2>/dev/null)" || cf_token=""
            cf_zone="$(config_get CF_ZONE_ID 2>/dev/null)" || cf_zone=""

            local cf_email_saved cf_key_saved
            cf_email_saved="$(config_get CF_EMAIL 2>/dev/null)" || cf_email_saved=""
            cf_key_saved="$(config_get CF_API_KEY 2>/dev/null)" || cf_key_saved=""

            if [[ -n "$cf_key_saved" && -n "$cf_zone" ]]; then
                printf "  ${GREEN}Configured${NC}\n"
                printf "  Zone ID: %s\n\n" "$cf_zone"
                printf "  1) Check cache rule status\n"
                printf "  2) Purge Cloudflare cache\n"
                printf "  3) Reconfigure\n"
                printf "  4) Remove (delete credentials + cache rule)\n"
                printf "  0) Back\n\n"
                local cf_sub
                read -rp "  Choose: " cf_sub
                case "$cf_sub" in
                    1)
                        local rules
                        rules="$(curl -sf -X GET "https://api.cloudflare.com/client/v4/zones/${cf_zone}/rulesets/phases/http_request_cache_settings/entrypoint" \
                            -H "X-Auth-Email: ${cf_email_saved}" -H "X-Auth-Key: ${cf_key_saved}" \
                            -H "Content-Type: application/json" 2>/dev/null)"
                        if echo "$rules" | grep -q "azwp-cache"; then
                            log_success "Cache rule 'azwp-cache' is active."
                        else
                            log_warn "Cache rule not found. Run reconfigure."
                        fi
                        ;;
                    2)
                        curl -sf -X POST "https://api.cloudflare.com/client/v4/zones/${cf_zone}/purge_cache" \
                            -H "X-Auth-Email: ${cf_email_saved}" -H "X-Auth-Key: ${cf_key_saved}" \
                            -H "Content-Type: application/json" \
                            -d '{"purge_everything":true}' > /dev/null 2>&1
                        log_success "Cloudflare cache purged."
                        ;;
                    3) cf_key_saved="" ;; # fall through to setup
                    4)
                        confirm "Remove Cloudflare integration? This deletes the cache rule." || return 0
                        # Delete cache rule
                        curl -sf -X PUT \
                            "https://api.cloudflare.com/client/v4/zones/${cf_zone}/rulesets/phases/http_request_cache_settings/entrypoint" \
                            -H "X-Auth-Email: ${cf_email_saved}" -H "X-Auth-Key: ${cf_key_saved}" \
                            -H "Content-Type: application/json" \
                            -d '{"rules":[]}' > /dev/null 2>&1 || true
                        # Remove credentials
                        sed -i '/^CF_/d' "$AZ_CONFIG_FILE" 2>/dev/null || true
                        log_success "Cloudflare credentials and cache rule removed."
                        return 0
                        ;;
                    *) return 0 ;;
                esac
                [[ -n "$cf_key_saved" ]] && return 0
            fi

            printf "  ${BOLD}Easiest: Use Global API Key${NC}\n\n"
            printf "  1. Go to: https://dash.cloudflare.com/profile/api-tokens\n"
            printf "  2. Scroll to ${BOLD}Global API Key${NC} → View → Copy\n"
            printf "  3. You also need your Cloudflare login email\n\n"

            local cf_email cf_key
            read -rp "  Cloudflare email: " cf_email
            [[ -z "$cf_email" ]] && { log_warn "Email required."; return 0; }
            read -rp "  Global API Key: " cf_key
            [[ -z "$cf_key" ]] && { log_warn "API Key required."; return 0; }

            # Build auth headers for Global API Key
            local cf_auth="-H \"X-Auth-Email: ${cf_email}\" -H \"X-Auth-Key: ${cf_key}\""

            # Auto-detect Zone ID from domain
            log_sub "Detecting Zone ID for $DOMAIN..."
            local root_domain
            root_domain="$(echo "$DOMAIN" | grep -oP '[^.]+\.[^.]+$')" || root_domain="$DOMAIN"
            local zone_response
            zone_response="$(curl -sf "https://api.cloudflare.com/client/v4/zones?name=${root_domain}" \
                -H "X-Auth-Email: ${cf_email}" -H "X-Auth-Key: ${cf_key}" \
                -H "Content-Type: application/json" 2>/dev/null)"

            cf_zone="$(echo "$zone_response" | grep -oP '"id":"[a-f0-9]{32}"' | head -1 | cut -d'"' -f4)" || cf_zone=""

            if [[ -z "$cf_zone" ]]; then
                log_error "Could not detect Zone ID. Check email and API Key."
                return 0
            fi
            log_success "Zone ID: $cf_zone"

            # Create Cache Rule — excludes wp-admin, wp-login, wp-json, logged-in users
            log_sub "Creating cache rule for $DOMAIN (public pages only)..."
            local cf_expr="(http.host eq \\\"${DOMAIN}\\\" and not starts_with(http.request.uri.path, \\\"/wp-admin\\\") and not starts_with(http.request.uri.path, \\\"/wp-login\\\") and not starts_with(http.request.uri.path, \\\"/wp-json\\\") and http.request.method eq \\\"GET\\\" and not http.cookie contains \\\"wordpress_logged_in\\\")"
            local rule_response
            rule_response="$(curl -sf -X PUT \
                "https://api.cloudflare.com/client/v4/zones/${cf_zone}/rulesets/phases/http_request_cache_settings/entrypoint" \
                -H "X-Auth-Email: ${cf_email}" -H "X-Auth-Key: ${cf_key}" \
                -H "Content-Type: application/json" \
                -d "{\"rules\":[{\"expression\":\"${cf_expr}\",\"description\":\"azwp-cache: Public pages for ${DOMAIN}\",\"action\":\"set_cache_settings\",\"action_parameters\":{\"cache\":true,\"edge_ttl\":{\"mode\":\"override_origin\",\"default\":86400},\"browser_ttl\":{\"mode\":\"override_origin\",\"default\":600}}}]}" 2>/dev/null)" || true

            if echo "$rule_response" | grep -q '"success":true\|"success": true'; then
                config_set "CF_EMAIL" "$cf_email"
                config_set "CF_API_KEY" "$cf_key"
                config_set "CF_ZONE_ID" "$cf_zone"
                log_success "Cloudflare cache rule created!"
                printf "\n  ${GREEN}HTML pages now cached at Cloudflare edge (24h TTL)${NC}\n"
                printf "  ${DIM}Browser: 10min. Edge: 24h. Purge: azwp advanced cloudflare${NC}\n"
            else
                local error_msg
                error_msg="$(echo "$rule_response" | grep -oP '"message":"[^"]+' | head -1 | cut -d'"' -f4)" || error_msg="Unknown error"
                log_error "Failed: $error_msg"
                printf "\n  ${DIM}If error persists, create the rule manually in Cloudflare Dashboard:${NC}\n"
                printf "  ${DIM}Caching → Cache Rules → Create Rule${NC}\n"
                printf "  ${DIM}When: hostname = %s → Cache eligible, Edge TTL 1 day${NC}\n" "$DOMAIN"
            fi
            ;;

        # --- Services ---
        services)
            local svc_sub="${extra}"
            if [[ -z "$svc_sub" ]]; then
                _header "Services Control"
                printf "  1) Restart all\n"
                printf "  2) Restart Nginx\n"
                printf "  3) Restart PHP-FPM\n"
                printf "  4) Restart MariaDB\n"
                printf "  5) Restart Redis\n"
                printf "  0) Back\n\n"
                read -rp "  Choose: " svc_sub
                case "$svc_sub" in 1) svc_sub="all" ;; 2) svc_sub="nginx" ;; 3) svc_sub="fpm" ;; 4) svc_sub="mariadb" ;; 5) svc_sub="redis" ;; *) return 0 ;; esac
            fi
            case "$svc_sub" in
                all) for s in nginx "php${PHP_VERSION}-fpm" mariadb redis-server; do service_restart "$s"; done ;;
                nginx) service_restart nginx ;;
                fpm) service_restart "php${PHP_VERSION}-fpm" ;;
                mariadb) service_restart mariadb ;;
                redis) service_restart redis-server ;;
            esac
            ;;

        # --- Workers ---
        workers)
            local w_sub="${extra}"
            if [[ -z "$w_sub" ]]; then
                _header "Workers Management"
                local web_active web_max wrk_active wrk_max
                web_active="$(curl -s http://127.0.0.1/fpm-status 2>/dev/null | grep 'active processes:' | head -1 | awk '{print $NF}')" || web_active="?"
                web_max="$(grep 'pm.max_children' /etc/php/${PHP_VERSION}/fpm/pool.d/web.conf 2>/dev/null | awk -F= '{print $2}' | tr -d ' ')" || web_max="?"
                wrk_active="$(curl -s http://127.0.0.1/fpm-workers-status 2>/dev/null | grep 'active processes:' | head -1 | awk '{print $NF}')" || wrk_active="?"
                wrk_max="$(grep 'pm.max_children' /etc/php/${PHP_VERSION}/fpm/pool.d/workers.conf 2>/dev/null | awk -F= '{print $2}' | tr -d ' ')" || wrk_max="?"
                printf "  Web pool:     %s / %s active\n" "$web_active" "$web_max"
                printf "  Workers pool: %s / %s active\n" "$wrk_active" "$wrk_max"
                printf "\n  Long-running (>30s):\n"
                ps -eo pid,etimes,pcpu,pmem,comm 2>/dev/null | grep php-fpm | awk '$2>30 {printf "    PID %-8s %ss  CPU:%s%%  MEM:%s%%\n", $1, $2, $3, $4}' || printf "    (none)\n"
                printf "\n  1) Kill stuck processes (>300s)\n  0) Back\n\n"
                read -rp "  Choose: " w_sub
                case "$w_sub" in 1) w_sub="kill-stuck" ;; *) return 0 ;; esac
            fi
            case "$w_sub" in
                kill-stuck)
                    local pids; pids="$(ps -eo pid,etimes,comm 2>/dev/null | grep php-fpm | awk '$2>300 {print $1}')"
                    if [[ -z "$pids" ]]; then log_info "No stuck processes."; return; fi
                    printf "  Will kill: %s\n" "$pids"
                    confirm "Kill these processes?" || return 0
                    echo "$pids" | xargs kill -9 2>/dev/null || true
                    log_success "Killed stuck processes."
                    ;;
            esac
            ;;

        # --- phpMyAdmin config ---
        pma-config)
            local pma_sub="${extra}"
            if [[ -z "$pma_sub" ]]; then
                _header "phpMyAdmin Config"
                local pma_path; pma_path="$(state_get PMA_PATH 2>/dev/null)" || pma_path=""
                if [[ -n "$pma_path" && -f /etc/nginx/azwp-pma.conf ]]; then
                    printf "  Status: ${GREEN}enabled${NC}\n"
                    printf "  URL:    https://%s%s/\n" "$DOMAIN" "$pma_path"
                else
                    printf "  Status: ${RED}disabled${NC}\n"
                fi
                printf "\n  1) Enable\n  2) Disable\n  3) Regenerate URL\n  0) Back\n\n"
                read -rp "  Choose: " pma_sub
                case "$pma_sub" in 1) pma_sub="enable" ;; 2) pma_sub="disable" ;; 3) pma_sub="regenerate" ;; *) return 0 ;; esac
            fi
            case "$pma_sub" in
                enable) _pma_enable ;;
                disable)
                    rm -f /etc/nginx/azwp-pma.conf /etc/phpmyadmin/conf.d/azwp-autologin.php /usr/share/phpmyadmin/az-wp-gate.php 2>/dev/null
                    local nginx_conf="/etc/nginx/sites-available/${DOMAIN}.conf"
                    sed -i '/azwp-pma.conf/d' "$nginx_conf" 2>/dev/null || true
                    service_reload nginx
                    log_success "phpMyAdmin disabled."
                    ;;
                regenerate)
                    rm -f /etc/nginx/azwp-pma.conf
                    local nginx_conf="/etc/nginx/sites-available/${DOMAIN}.conf"
                    sed -i '/azwp-pma.conf/d' "$nginx_conf" 2>/dev/null || true
                    _pma_enable
                    ;;
            esac
            ;;

        # --- Update Scripts ---
        update-scripts)
            local az_dir
            az_dir="$(state_get AZ_DIR 2>/dev/null || echo /opt/azwp)"

            if [[ ! -d "${az_dir}/.git" ]]; then
                log_error "az-wp repo not found at ${az_dir}."
                return 1
            fi

            _header "Update azwp Scripts"

            # Show current version
            local current_ver
            current_ver="$(cat "${az_dir}/VERSION" 2>/dev/null || echo 'unknown')"
            printf "  Current version: ${BOLD}%s${NC}\n" "$current_ver"
            printf "  Install dir:     %s\n\n" "$az_dir"

            log_info "Pulling latest from repository..."
            cd "$az_dir"
            local before_hash; before_hash="$(git rev-parse HEAD 2>/dev/null)"
            local fetch_output
            fetch_output="$(GIT_TERMINAL_PROMPT=0 git fetch origin 2>&1)" || {
                log_error "Git fetch failed."
                local remote_url; remote_url="$(git remote get-url origin 2>/dev/null || echo '')"
                if [[ "$remote_url" == https://* ]]; then
                    printf "\n  Remote uses HTTPS but repo may be private.\n"
                    printf "  Fix: ${BOLD}azwp advanced deploy-key setup${NC} (switch to SSH)\n"
                else
                    printf "\n  Remote uses SSH: %s\n" "$remote_url"
                    printf "  Fix: ${BOLD}azwp advanced deploy-key test${NC} (check key access)\n"
                fi
                return 1
            }

            local behind
            behind="$(git rev-list HEAD..origin/main --count 2>/dev/null || echo 0)"
            if [[ "$behind" -eq 0 ]]; then
                log_success "Scripts are already up to date."
                return 0
            fi

            log_sub "${behind} update(s) available."

            # Show changed script files
            local changed_scripts
            changed_scripts="$(git diff --name-only HEAD..origin/main -- single/ lib/ setup.sh 2>/dev/null || true)"
            if [[ -n "$changed_scripts" ]]; then
                printf "\n  ${BOLD}Changed scripts:${NC}\n"
                echo "$changed_scripts" | while read -r f; do printf "    ${GREEN}%s${NC}\n" "$f"; done
                printf "\n"
            fi

            # Show changed packages
            local changed_pkgs
            changed_pkgs="$(git diff --name-only HEAD..origin/main -- clone/*.zip 2>/dev/null || true)"
            if [[ -n "$changed_pkgs" ]]; then
                printf "  ${BOLD}Updated packages:${NC}\n"
                echo "$changed_pkgs" | while read -r f; do printf "    ${CYAN}%s${NC}\n" "$(basename "$f")"; done
                printf "\n"
            fi

            git pull --ff-only origin main 2>&1 | head -5

            local after_hash; after_hash="$(git rev-parse HEAD 2>/dev/null)"
            if [[ "$before_hash" != "$after_hash" ]]; then
                local new_ver; new_ver="$(cat "${az_dir}/VERSION" 2>/dev/null || echo 'unknown')"
                log_success "Scripts updated! v${current_ver} → v${new_ver}"
                if [[ -n "$changed_pkgs" ]]; then
                    printf "\n  ${BOLD}Tip:${NC} New packages available. Run ${BOLD}azwp update all${NC} to install them.\n"
                fi
            else
                log_warn "Pull completed but no changes applied."
            fi
            ;;

        # --- GitHub Deploy Key ---
        deploy-key)
            local dk_sub="${extra}"
            local key_file="/root/.ssh/azwp_deploy"
            local az_dir; az_dir="$(state_get AZ_DIR 2>/dev/null || echo /opt/azwp)"
            local has_key=false
            local has_ssh_remote=false
            local access_ok=false
            local remote_url; remote_url="$(cd "$az_dir" && git remote get-url origin 2>/dev/null || echo "")"

            [[ -f "${key_file}.pub" ]] && has_key=true
            [[ "$remote_url" == git@* ]] && has_ssh_remote=true

            if [[ -z "$dk_sub" ]]; then
                _header "GitHub Deploy Key"

                # --- Status dashboard ---
                if $has_key; then
                    printf "  Key:     ${GREEN}Installed${NC} (${key_file})\n"
                    # Fingerprint
                    local fp; fp="$(ssh-keygen -lf "${key_file}.pub" 2>/dev/null | awk '{print $2}')" || fp=""
                    [[ -n "$fp" ]] && printf "  ID:      ${DIM}%s${NC}\n" "$fp"
                else
                    printf "  Key:     ${RED}Not installed${NC}\n"
                fi

                if $has_ssh_remote; then
                    printf "  Remote:  ${GREEN}SSH${NC} (%s)\n" "$remote_url"
                else
                    printf "  Remote:  ${YELLOW}HTTPS${NC} (%s)\n" "$remote_url"
                fi

                if $has_key && $has_ssh_remote; then
                    local _ssh_out
                    _ssh_out="$(ssh -o StrictHostKeyChecking=no -o ConnectTimeout=5 -o BatchMode=yes -i "$key_file" -T git@github.com 2>&1 || true)"
                    if echo "$_ssh_out" | grep -qi "success\|granted"; then
                        access_ok=true
                        printf "  Access:  ${GREEN}Connected${NC}\n"
                    else
                        printf "  Access:  ${RED}Failed${NC} (key not added to GitHub?)\n"
                    fi
                elif $has_key; then
                    printf "  Access:  ${YELLOW}Not configured${NC} (remote still HTTPS)\n"
                fi

                # --- Context-aware menu ---
                printf "\n"
                if ! $has_key; then
                    printf "  1) Setup (generate key + configure SSH + switch to SSH)\n"
                    printf "  0) Back\n\n"
                    read -rp "  Choose: " dk_sub
                    case "$dk_sub" in 1) dk_sub="setup" ;; *) return 0 ;; esac
                elif ! $has_ssh_remote; then
                    printf "  1) Configure SSH + switch remote\n"
                    printf "  2) Show public key (copy to GitHub)\n"
                    printf "  3) Test connection\n"
                    printf "  4) Regenerate key (replace current)\n"
                    printf "  5) Remove key\n"
                    printf "  0) Back\n\n"
                    read -rp "  Choose: " dk_sub
                    case "$dk_sub" in 1) dk_sub="setup" ;; 2) dk_sub="show" ;; 3) dk_sub="test" ;; 4) dk_sub="regenerate" ;; 5) dk_sub="remove" ;; *) return 0 ;; esac
                else
                    printf "  1) Test connection\n"
                    printf "  2) Show public key\n"
                    printf "  3) Regenerate key (replace current)\n"
                    printf "  4) Switch back to HTTPS\n"
                    printf "  5) Remove key\n"
                    printf "  0) Back\n\n"
                    read -rp "  Choose: " dk_sub
                    case "$dk_sub" in 1) dk_sub="test" ;; 2) dk_sub="show" ;; 3) dk_sub="regenerate" ;; 4) dk_sub="to-https" ;; 5) dk_sub="remove" ;; *) return 0 ;; esac
                fi
            fi

            case "$dk_sub" in
                setup)
                    # All-in-one: generate + SSH config + switch remote
                    if ! $has_key; then
                        ssh-keygen -t ed25519 -C "azwp-deploy@$(hostname)-${DOMAIN}" -f "$key_file" -N "" 2>/dev/null
                        chmod 600 "$key_file"
                        log_success "Deploy key generated."
                    fi

                    if ! grep -q "azwp_deploy" /root/.ssh/config 2>/dev/null; then
                        mkdir -p /root/.ssh
                        cat >> /root/.ssh/config << 'SSHEOF'

# az-wp deploy key for GitHub
Host github.com
    HostName github.com
    User git
    IdentityFile /root/.ssh/azwp_deploy
    IdentitiesOnly yes
    StrictHostKeyChecking no
SSHEOF
                        chmod 600 /root/.ssh/config
                        log_success "SSH config updated."
                    fi

                    if [[ "$remote_url" != git@* ]]; then
                        cd "$az_dir" && git remote set-url origin "git@github.com:azsmarthub/az-wp.git"
                        log_success "Remote switched to SSH."
                    fi

                    printf "\n  ${BOLD}Public key (add to GitHub):${NC}\n\n"
                    printf "  ${CYAN}%s${NC}\n\n" "$(cat "${key_file}.pub")"
                    printf "  ${BOLD}Add at:${NC} https://github.com/azsmarthub/az-wp/settings/keys\n"
                    printf "  Title: ${GREEN}VPS - %s${NC} | Do NOT check 'Allow write access'\n\n" "$DOMAIN"
                    printf "  After adding → ${BOLD}azwp advanced deploy-key test${NC}\n"
                    ;;
                regenerate)
                    printf "\n  This will replace the current key. You'll need to update GitHub.\n"
                    read -rp "  Continue? (y/N): " confirm
                    [[ "$confirm" != "y" && "$confirm" != "Y" ]] && return 0
                    rm -f "$key_file" "${key_file}.pub"
                    ssh-keygen -t ed25519 -C "azwp-deploy@$(hostname)-${DOMAIN}" -f "$key_file" -N "" 2>/dev/null
                    chmod 600 "$key_file"
                    log_success "New key generated."
                    printf "\n  ${BOLD}Replace the old key on GitHub with this new one:${NC}\n\n"
                    printf "  ${CYAN}%s${NC}\n\n" "$(cat "${key_file}.pub")"
                    printf "  ${BOLD}URL:${NC} https://github.com/azsmarthub/az-wp/settings/keys\n"
                    ;;
                show)
                    if ! $has_key; then
                        log_warn "No key found. Run: azwp advanced deploy-key setup"
                        return 1
                    fi
                    printf "\n  ${BOLD}Public key:${NC}\n\n"
                    printf "  ${CYAN}%s${NC}\n\n" "$(cat "${key_file}.pub")"
                    printf "  ${BOLD}Add at:${NC} https://github.com/azsmarthub/az-wp/settings/keys\n"
                    ;;
                test)
                    if ! $has_key; then
                        log_error "No key found. Run: azwp advanced deploy-key setup"
                        return 1
                    fi
                    printf "\n  Testing GitHub SSH connection...\n\n"
                    local test_output
                    test_output="$(ssh -o StrictHostKeyChecking=no -o ConnectTimeout=10 -i "$key_file" -T git@github.com 2>&1 || true)"
                    printf "  %s\n\n" "$test_output"
                    if echo "$test_output" | grep -qi "success\|granted"; then
                        log_success "Connected! Ready to use: azwp update pull-update"
                    else
                        log_error "Connection failed."
                        printf "  → Add key to GitHub: https://github.com/azsmarthub/az-wp/settings/keys\n"
                        printf "  → Show key: ${BOLD}azwp advanced deploy-key show${NC}\n"
                    fi
                    ;;
                to-https)
                    cd "$az_dir" && git remote set-url origin "https://github.com/azsmarthub/az-wp.git"
                    log_success "Switched back to HTTPS."
                    printf "  ${DIM}Note: HTTPS only works with public repos.${NC}\n"
                    ;;
                remove)
                    printf "\n  This removes the deploy key and SSH config.\n"
                    read -rp "  Continue? (y/N): " confirm
                    [[ "$confirm" != "y" && "$confirm" != "Y" ]] && return 0
                    rm -f "$key_file" "${key_file}.pub"
                    # Remove SSH config block
                    if [[ -f /root/.ssh/config ]]; then
                        sed -i '/# az-wp deploy key/,/StrictHostKeyChecking/d' /root/.ssh/config 2>/dev/null || true
                        sed -i '/^$/N;/^\n$/d' /root/.ssh/config 2>/dev/null || true
                    fi
                    # Switch back to HTTPS
                    cd "$az_dir" && git remote set-url origin "https://github.com/azsmarthub/az-wp.git" 2>/dev/null || true
                    log_success "Deploy key removed. Remote switched to HTTPS."
                    ;;
            esac
            ;;

        *) log_warn "Unknown advanced command: $sub" ;;
    esac
}

# ===========================================================================
# phpMyAdmin enable helper
# ===========================================================================
_pma_enable() {
    local nginx_conf="/etc/nginx/sites-available/${DOMAIN}.conf"

    # Install if missing
    if [[ ! -d /usr/share/phpmyadmin ]]; then
        log_info "Installing phpMyAdmin..."
        NEEDRESTART_MODE=a DEBIAN_FRONTEND=noninteractive apt-get install -y phpmyadmin > /dev/null 2>&1 \
            || die "Failed to install phpMyAdmin."
        log_success "phpMyAdmin installed."
    fi

    # Generate base64 URL path (stronger than hex, URL-safe)
    local pma_rand
    pma_rand="$(openssl rand -base64 32 | tr -d '/+=' | cut -c1-24)"
    local pma_path="/db-${pma_rand}"

    # Record expiry (24 hours from now)
    local pma_expiry
    pma_expiry="$(date -d '+24 hours' '+%Y-%m-%d %H:%M:%S' 2>/dev/null || date -v+24H '+%Y-%m-%d %H:%M:%S' 2>/dev/null)"

    # FPM pool
    local pma_pool="/etc/php/${PHP_VERSION}/fpm/pool.d/phpmyadmin.conf"
    if [[ ! -f "$pma_pool" ]]; then
        cat > "$pma_pool" <<FPMPOOL
[phpmyadmin]
user = www-data
group = www-data
listen = /run/php/php${PHP_VERSION}-fpm-pma.sock
listen.owner = www-data
listen.group = www-data
listen.mode = 0660
pm = ondemand
pm.max_children = 2
pm.process_idle_timeout = 60s
request_terminate_timeout = 300
php_admin_value[open_basedir] = /usr/share/phpmyadmin:/tmp:/var/lib/phpmyadmin:/etc/phpmyadmin:/usr/share/php
php_admin_value[upload_max_filesize] = 256M
php_admin_value[post_max_size] = 256M
php_admin_value[max_execution_time] = 300
FPMPOOL
        systemctl restart "php${PHP_VERSION}-fpm"
    fi

    # Nginx snippet
    cat > /etc/nginx/azwp-pma.conf <<PMACONF
    # phpMyAdmin — managed by azwp (expires: ${pma_expiry})
    location ^~ ${pma_path}/ {
        alias /usr/share/phpmyadmin/;
        index index.php;
        location ~ \.php\$ {
            include fastcgi_params;
            fastcgi_param SCRIPT_FILENAME \$request_filename;
            fastcgi_pass unix:/run/php/php${PHP_VERSION}-fpm-pma.sock;
        }
    }
    # end phpMyAdmin
PMACONF

    sed -i '/azwp-pma.conf/d' "$nginx_conf" 2>/dev/null || true
    if ! grep -q "azwp-pma.conf" "$nginx_conf"; then
        sed -i '/^}$/i \    include /etc/nginx/azwp-pma.conf;' "$nginx_conf"
    fi

    # Auto-login config
    mkdir -p /etc/phpmyadmin/conf.d
    local db_user db_pass
    db_user="$(state_get DB_USER)" || db_user=""
    db_pass="$(state_get DB_PASS)" || db_pass=""
    cat > /etc/phpmyadmin/conf.d/azwp-autologin.php <<PHPCFG
<?php
\$cfg['Servers'][1]['auth_type'] = 'config';
\$cfg['Servers'][1]['user'] = '${db_user}';
\$cfg['Servers'][1]['password'] = '${db_pass}';
\$cfg['Servers'][1]['AllowNoPassword'] = false;
PHPCFG

    # Setup auto-disable cron (24h)
    local disable_at
    disable_at="$(date -d '+24 hours' '+%M %H %d %m *' 2>/dev/null || date -v+24H '+%M %H %d %m *' 2>/dev/null)"
    cat > /etc/cron.d/azwp-pma-expire <<CRONCFG
# azwp: auto-disable phpMyAdmin after 24h
${disable_at} root /usr/local/bin/azwp advanced pma-config disable > /dev/null 2>&1 && rm -f /etc/cron.d/azwp-pma-expire
CRONCFG
    chmod 644 /etc/cron.d/azwp-pma-expire

    if nginx -t 2>/dev/null; then
        service_reload nginx
    else
        log_error "Nginx config test failed."
        rm -f /etc/nginx/azwp-pma.conf
        sed -i '/azwp-pma.conf/d' "$nginx_conf"
        return 1
    fi

    state_set "PMA_PATH" "$pma_path"
    state_set "PMA_EXPIRY" "$pma_expiry"
    log_success "phpMyAdmin enabled (auto-disables in 24h)."
    printf "\n  ${BOLD}URL:${NC} https://%s%s/\n" "$DOMAIN" "$pma_path"
    printf "  ${DIM}Expires: %s${NC}\n\n" "$pma_expiry"
}

# ===========================================================================
# UPDATE - AffiliateCMS plugin/theme updater from az-wp repo
# ===========================================================================

# Component map: shortname → zip filename | WP install type | WP slug
_ACMS_COMPONENTS=(
    "pro|affiliatecms-pro.zip|plugin|affiliatecms-pro"
    "cat|affiliatecms-cat.zip|plugin|affiliatecms-cat"
    "ai|affiliatecms-ai.zip|plugin|affiliatecms-ai"
    "theme|affiliateCMS-theme.zip|theme|affiliateCMS-theme"
    "child-theme|affiliateCMS-Child-theme.zip|theme|affiliateCMS-Child-theme"
)

_update_component() {
    local short="$1"
    local zip_file="" install_type="" slug=""

    for comp in "${_ACMS_COMPONENTS[@]}"; do
        IFS='|' read -r s z t sl <<< "$comp"
        if [[ "$s" == "$short" ]]; then
            zip_file="$z"; install_type="$t"; slug="$sl"
            break
        fi
    done

    if [[ -z "$zip_file" ]]; then
        log_error "Unknown component: $short"
        return 1
    fi

    local az_dir
    az_dir="$(state_get AZ_DIR 2>/dev/null || echo /opt/azwp)"
    local zip_path="${az_dir}/clone/${zip_file}"

    if [[ ! -f "$zip_path" ]]; then
        log_error "Zip not found: ${zip_path}"
        log_sub "Run 'azwp update pull' first to fetch latest packages."
        return 1
    fi

    log_info "Updating ${slug} from ${zip_file}..."

    if [[ "$install_type" == "plugin" ]]; then
        sudo -u "$SITE_USER" wp plugin install "$zip_path" --force --activate --path="$WEB_ROOT" 2>&1 | grep -v Deprecated || true
    else
        sudo -u "$SITE_USER" wp theme install "$zip_path" --force --path="$WEB_ROOT" 2>&1 | grep -v Deprecated || true
    fi

    log_success "${slug} updated."
}

_update_pull() {
    local az_dir
    az_dir="$(state_get AZ_DIR 2>/dev/null || echo /opt/azwp)"

    if [[ ! -d "${az_dir}/.git" ]]; then
        log_error "az-wp repo not found at ${az_dir}. Cannot pull updates."
        return 1
    fi

    log_info "Pulling latest from az-wp repository..."
    cd "$az_dir"
    local fetch_out
    fetch_out="$(GIT_TERMINAL_PROMPT=0 git fetch origin 2>&1)" || {
        log_error "Git fetch failed. Run: azwp advanced deploy-key setup"
        return 1
    }

    # Show what's new
    local behind
    behind="$(git rev-list HEAD..origin/main --count 2>/dev/null || echo 0)"
    if [[ "$behind" -eq 0 ]]; then
        log_success "Already up to date."
        return 0
    fi

    log_sub "${behind} new commit(s) available."

    # Pull changes (fast-forward only, safe)
    git pull --ff-only origin main 2>&1 | head -5

    if [[ $? -eq 0 ]]; then
        log_success "Updated. ${behind} commit(s) applied."
        # Show which zips changed
        local changed
        changed="$(git diff --name-only HEAD~${behind} HEAD -- clone/*.zip 2>/dev/null || true)"
        if [[ -n "$changed" ]]; then
            log_sub "Updated packages:"
            echo "$changed" | while read -r f; do printf "    ${GREEN}%s${NC}\n" "$(basename "$f")"; done
        fi
    else
        log_warn "Fast-forward failed. You may have local changes."
        log_sub "Try: cd ${az_dir} && git reset --hard origin/main"
    fi
}

_update_seed_prompts() {
    log_info "Seeding AI prompt defaults..."
    sudo -u "$SITE_USER" wp eval 'if (class_exists("AffiliateCMS\AI\Core\DefaultPrompts")) { AffiliateCMS\AI\Core\DefaultPrompts::seed(); echo "OK"; }' --path="$WEB_ROOT" 2>/dev/null | grep -q OK \
        && log_success "AI prompts seeded." \
        || log_warn "Could not seed prompts (plugin may not be active)."
}

# ===========================================================================
# FIX - one-shot migrations for existing sites after pulling new azwp code
# ===========================================================================

# Patch the old anti-enumeration rule on /wp-json/wp/v2/users that broke
# Gutenberg's Author dropdown (it fetches context=view&who=authors and got
# a flat 403 from nginx before reaching WordPress). Replaces the block
# with a cookie-aware version: anonymous still 403, logged-in users pass
# through to WP which applies its own capability check.
_fix_nginx_users_rule() {
    local conf="/etc/nginx/sites-available/${DOMAIN}.conf"
    [[ -f "$conf" ]] || { log_error "nginx vhost not found: $conf"; return 1; }

    if grep -q 'users_allowed' "$conf"; then
        log_info "Nginx users rule already patched — nothing to do."
        return 0
    fi

    if ! grep -q 'arg_context != "edit"' "$conf"; then
        log_warn "Old users rule signature not found in $conf — skipping."
        return 0
    fi

    local new_block
    new_block=$(cat <<NGBLOCK
    # Block anonymous user enumeration via REST API, allow logged-in users.
    location ~ ^/wp-json/wp/v2/users {
        set \$users_allowed 0;
        if (\$http_cookie ~* "wordpress_logged_in_") { set \$users_allowed 1; }
        if (\$arg_context = "edit") { set \$users_allowed 1; }
        if (\$users_allowed != 1) { return 403; }

        include fastcgi_params;
        fastcgi_param SCRIPT_FILENAME \$document_root/index.php;
        fastcgi_pass unix:/run/php/php${PHP_VERSION}-fpm-web.sock;
    }
NGBLOCK
)

    # Replace the whole block using brace counting (handles nested {} from the
    # inner `if (...)` directives correctly).
    awk -v new_block="$new_block" '
        /^[[:space:]]*location ~ \^\/wp-json\/wp\/v2\/users \{/ {
            print new_block
            depth = 1
            in_block = 1
            # Swallow the opening brace from the original line
            for (i = 1; i <= length($0); i++) {
                c = substr($0, i, 1)
                if (c == "{") depth_adj = 1
            }
            next
        }
        in_block {
            for (i = 1; i <= length($0); i++) {
                c = substr($0, i, 1)
                if (c == "{") depth++
                if (c == "}") depth--
            }
            if (depth <= 0) { in_block = 0 }
            next
        }
        { print }
    ' "$conf" > "${conf}.tmp" && mv "${conf}.tmp" "$conf"

    if ! nginx -t >/dev/null 2>&1; then
        log_error "nginx config test failed after patch"
        nginx -t 2>&1 | tail -5
        return 1
    fi
    service_reload nginx
    log_success "Patched nginx users rule in ${conf}"
    log_sub "Gutenberg author dropdown should now populate correctly for logged-in users."
}

menu_fix() {
    local sub="${1:-}"

    if [[ -z "$sub" ]]; then
        _header "Fix — one-shot migrations"
        printf "  ${DIM}Apply migrations after 'azwp update pull' brings new code.${NC}\n\n"
        printf "  1) nginx-users   Patch /wp-json/wp/v2/users rule (fix Gutenberg author dropdown)\n"
        printf "  0) Back\n\n"
        read -rp "  Choose: " sub
        case "$sub" in 1) sub="nginx-users" ;; *) return 0 ;; esac
    fi

    case "$sub" in
        nginx-users)
            _fix_nginx_users_rule
            ;;
        *)
            log_warn "Unknown fix: $sub"
            printf "  Usage: azwp fix [nginx-users]\n"
            ;;
    esac
}

menu_update() {
    local sub="${1:-}"

    if [[ -z "$sub" ]]; then
        _header "AffiliateCMS Update"
        printf "  ${BOLD}Components:${NC}\n"
        printf "    1) pro          AffiliateCMS Pro plugin\n"
        printf "    2) cat          AffiliateCMS Categories plugin\n"
        printf "    3) ai           AffiliateCMS AI plugin\n"
        printf "    4) theme        AffiliateCMS Theme (parent)\n"
        printf "    5) child-theme  AffiliateCMS Child Theme ${DIM}(customized, manual only)${NC}\n"
        printf "    6) all          Update everything ${DIM}(excludes child theme)${NC}\n"
        printf "\n  ${BOLD}Other:${NC}\n"
        printf "    7) pull         Pull latest packages from GitHub\n"
        printf "    8) pull-update  Pull + update all components\n"
        printf "    9) seed         Seed AI prompt defaults\n"
        printf "\n    0) Back\n\n"
        read -rp "  Choose: " sub
        case "$sub" in
            1) sub="pro" ;; 2) sub="cat" ;; 3) sub="ai" ;;
            4) sub="theme" ;; 5) sub="child-theme" ;; 6) sub="all" ;;
            7) sub="pull" ;; 8) sub="pull-update" ;; 9) sub="seed" ;;
            *) return 0 ;;
        esac
    fi

    case "$sub" in
        pro|cat|ai|theme|child-theme)
            _update_component "$sub"
            ;;
        all)
            # Child theme is customized per-site during install (domain,
            # branding in functions.php) — excluded from "all" so updates
            # don't wipe user customizations. Use menu option 5/child-theme
            # explicitly when you want to reinstall it from the clone zip.
            for comp in "${_ACMS_COMPONENTS[@]}"; do
                IFS='|' read -r short _ _ _ <<< "$comp"
                [[ "$short" == "child-theme" ]] && continue
                _update_component "$short"
            done
            _update_seed_prompts
            log_info "Purging caches..."
            sudo -u "$SITE_USER" wp cache flush --path="$WEB_ROOT" 2>/dev/null || true
            [[ -d "$CACHE_PATH" ]] && rm -rf "${CACHE_PATH:?}"/* 2>/dev/null || true
            log_success "All components updated (child theme skipped — customized)."
            ;;
        pull)
            _update_pull
            ;;
        pull-update)
            _update_pull
            printf "\n"
            menu_update all
            ;;
        seed)
            _update_seed_prompts
            ;;
        *)
            log_warn "Unknown: $sub"
            printf "  Usage: azwp update [pro|cat|ai|theme|child-theme|all|pull|pull-update|seed]\n"
            ;;
    esac
}

