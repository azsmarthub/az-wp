menu_wordpress() {
    local sub="${1:-}"
    if [[ -z "$sub" ]]; then
        _header "WordPress Tools"
        printf "  1) Login (auto-login URL, no password)\n"
        printf "  2) Update (AffiliateCMS + WP core)\n"
        printf "  3) List plugins\n"
        printf "  4) Debug mode on/off\n"
        printf "  5) Maintenance mode on/off\n"
        printf "  6) Reset admin password\n"
        printf "  7) Search & replace\n"
        printf "  8) WP-CLI (pass-through)\n"
        printf "  0) Back\n\n"
        read -rp "  Choose: " sub
        case "$sub" in
            1) sub="login" ;; 2) sub="update" ;; 3) sub="plugins" ;; 4) sub="debug" ;;
            5) sub="maintenance" ;; 6) sub="admin-pass" ;; 7) sub="search-replace" ;;
            8) sub="cli" ;; *) return 0 ;;
        esac
    fi

    case "$sub" in
        login)
            _wp_auto_login
            ;;
        update)
            local up_sub="${2:-}"
            if [[ -z "$up_sub" ]]; then
                _header "Update"
                printf "  ${BOLD}AffiliateCMS:${NC}\n"
                printf "    1) Pull + Update All    (pull latest + install, ${DIM}excludes child theme${NC})\n"
                printf "    2) Pro plugin           affiliatecms-pro\n"
                printf "    3) Categories plugin    affiliatecms-cat\n"
                printf "    4) AI plugin            affiliatecms-ai\n"
                printf "    5) Theme (parent)       affiliateCMS-theme\n"
                printf "    6) Child Theme          affiliateCMS-Child-theme ${DIM}(customized, manual only)${NC}\n"
                printf "\n  ${BOLD}WordPress:${NC}\n"
                printf "    7) WP Core + WP plugins + WP themes (from wordpress.org)\n"
                printf "\n  ${BOLD}Utilities:${NC}\n"
                printf "    8) Seed AI prompts\n"
                printf "    9) Pull only (download packages, don't install)\n"
                printf "\n    0) Back\n\n"
                read -rp "  Choose: " up_sub
                case "$up_sub" in
                    1) up_sub="pull-update" ;; 2) up_sub="pro" ;; 3) up_sub="cat" ;;
                    4) up_sub="ai" ;; 5) up_sub="theme" ;; 6) up_sub="child-theme" ;;
                    7) up_sub="wp-core" ;; 8) up_sub="seed" ;; 9) up_sub="pull" ;;
                    *) return 0 ;;
                esac
            fi
            case "$up_sub" in
                wp-core)
                    log_info "Updating WordPress core, plugins, and themes..."
                    wp_run core update
                    wp_run plugin update --all
                    wp_run theme update --all
                    log_success "WordPress updated."
                    ;;
                *)
                    # Delegate to azwp update command
                    menu_update "$up_sub"
                    ;;
            esac
            ;;
        plugins)
            wp_run plugin list
            ;;
        debug)
            local current; current="$(wp_run config get WP_DEBUG --raw 2>/dev/null | tr -d '[:space:]')"
            local debug_arg="${2:-}"
            local is_on=false
            [[ "$current" == "1" || "$current" == "true" ]] && is_on=true

            if [[ "$debug_arg" == "on" ]]; then
                wp_run config set WP_DEBUG true --raw > /dev/null
                wp_run config set WP_DEBUG_LOG true --raw > /dev/null
                log_success "Debug mode → ON. Logs: $WEB_ROOT/wp-content/debug.log"
            elif [[ "$debug_arg" == "off" ]]; then
                wp_run config set WP_DEBUG false --raw > /dev/null
                wp_run config set WP_DEBUG_LOG false --raw > /dev/null
                log_success "Debug mode → OFF."
            else
                # Show status + interactive toggle menu
                if $is_on; then
                    printf "  Debug mode: ${YELLOW}ON${NC}\n"
                    printf "  Log: %s/wp-content/debug.log\n" "$WEB_ROOT"
                else
                    printf "  Debug mode: ${GREEN}OFF${NC}\n"
                fi
                printf "\n  1) Turn ON\n"
                printf "  2) Turn OFF\n"
                printf "  0) Back\n\n"
                local choice; read -rp "  Choose: " choice
                case "$choice" in
                    1)
                        wp_run config set WP_DEBUG true --raw > /dev/null
                        wp_run config set WP_DEBUG_LOG true --raw > /dev/null
                        log_success "Debug mode → ON. Logs: $WEB_ROOT/wp-content/debug.log"
                        ;;
                    2)
                        wp_run config set WP_DEBUG false --raw > /dev/null
                        wp_run config set WP_DEBUG_LOG false --raw > /dev/null
                        log_success "Debug mode → OFF."
                        ;;
                    *) return 0 ;;
                esac
            fi
            ;;
        maintenance)
            local mfile="$WEB_ROOT/.maintenance"
            if [[ -f "$mfile" ]]; then
                rm -f "$mfile"
                log_success "Maintenance mode OFF."
            else
                printf '<?php $upgrading = time(); ?>' > "$mfile"
                chown "$SITE_USER:$SITE_USER" "$mfile"
                log_success "Maintenance mode ON."
            fi
            ;;
        admin-pass)
            local new_pass; new_pass="$(generate_password 20)"
            wp_run user update "$(state_get WP_ADMIN_USER 2>/dev/null || echo admin)" --user_pass="$new_pass"
            log_success "Admin password changed: $new_pass"
            ;;
        search-replace)
            local old_str new_str
            read -rp "  Old string: " old_str
            read -rp "  New string: " new_str
            [[ -z "$old_str" || -z "$new_str" ]] && { log_warn "Empty input."; return; }
            log_info "Dry run..."
            wp_run search-replace "$old_str" "$new_str" --dry-run
            confirm "Apply changes?" || return 0
            wp_run search-replace "$old_str" "$new_str"
            log_success "Search & replace complete."
            ;;
        cli)
            printf "  Entering WP-CLI (type 'exit' to return):\n"
            sudo -u "$SITE_USER" wp shell --path="$WEB_ROOT" 2>/dev/null || \
                sudo -u "$SITE_USER" bash -c "cd $WEB_ROOT && exec bash"
            ;;
        *) log_warn "Unknown: $sub" ;;
    esac
}

