                    echo | openssl s_client -connect localhost:443 -servername "$DOMAIN" 2>/dev/null \
                        | openssl x509 -noout -subject -issuer -dates 2>/dev/null || log_warn "No SSL cert found."
                    ;;
            esac
            ;;

        # --- Security ---
        security)
