# Get cron filename by number
_cron_get_by_number() {
    local num="$1"
    local i=0
    for f in /etc/cron.d/az-wp-*; do
        [[ ! -f "$f" ]] && continue
        [[ "$(basename "$f")" == "az-wp-cron" ]] && continue
        i=$((i + 1))
        if [[ $i -eq $num ]]; then
            basename "$f"
            return 0
        fi
    done
    return 1
}

# Add a single cron from a raw line
_cron_add_line() {
    local cron_line="$1"

    # Auto-generate name from URL path
    local cron_name
    cron_name="$(printf '%s' "$cron_line" | grep -oP '/v1/[^?"]+' | sed 's|/v1/||;s|/|-|g' | head -1 || true)"
    [[ -z "$cron_name" ]] && cron_name="custom-$(openssl rand -hex 3)"

    # Extract schedule (first 5 fields) and command (rest)
    local sched cmd
    sched="$(printf '%s' "$cron_line" | awk '{print $1,$2,$3,$4,$5}')"
    cmd="$(printf '%s' "$cron_line" | awk '{for(i=6;i<=NF;i++) printf "%s ", $i}')"
    cmd="${cmd% }"

    # Strip existing redirect (will re-add at the end)
    cmd="$(printf '%s' "$cmd" | sed 's|>>* */dev/null[^"]*||; s|2>&1||g')"
    cmd="${cmd% }"

    # Auto-add --resolve if curl without it (loopback optimization)
    if [[ "$cmd" == *curl* && "$cmd" != *--resolve* && "$cmd" == *"${DOMAIN}"* ]]; then
        cmd="$(printf '%s' "$cmd" | sed "s|curl |curl -sk --resolve ${DOMAIN}:443:127.0.0.1 --max-time 30 |")"
    fi

    # Always add clean redirect
    cmd="$cmd > /dev/null 2>&1"

    local cron_file="/etc/cron.d/az-wp-${cron_name}"
    printf '# az-wp cron: %s\n%s root %s\n' \
        "$cron_name" "$sched" "$cmd" > "$cron_file"
    chmod 644 "$cron_file"
    log_success "Created: $cron_file"
}

# Install AffiliateCMS preset (15 cron jobs)
_cron_install_preset() {
    _header "AffiliateCMS Cron Preset"

    # Get API token
    local api_token
    api_token="$(wp_run option get acms_api_token 2>/dev/null | tr -d '[:space:]')" || api_token=""

    if [[ -z "$api_token" ]]; then
        printf "  ${YELLOW}AffiliateCMS API token not found.${NC}\n"
        read -rp "  Enter API token: " api_token
        [[ -z "$api_token" ]] && { log_warn "Token required. Skipped."; return 0; }
    else
        printf "  API token: ${GREEN}found${NC}\n"
    fi

    # Loopback curl command (bypasses Cloudflare, connects to localhost)
    local base="curl -sk --resolve ${DOMAIN}:443:127.0.0.1"
    local url="https://${DOMAIN}"

    # All preset crons: name|schedule|endpoint|max_time|method|description
    # Tier 1: Pipeline (every 2 min, staggered with sleep offsets)
    # Tier 2: AI Generation (every 5 min, staggered)
    # Tier 3: Maintenance (background — less frequent)
    #
    # Stagger pattern: related crons offset by 20-30s to avoid race conditions
    # scrape(0s) → process-scheduled(20s) → scrape-monitor(40s) → queue-processor(60s) → queue-monitor(80s)
    # product-ai(0s) → post-ai(30s) → category-ai(60s) → brand-ai(90s) → brand-category-ai(120s)
    local presets=(
        "scrape|*/2 * * * *||/wp-json/acms/v1/automation/scrape|30|GET|Scrape dispatcher"
        "process-scheduled|*/2 * * * *|sleep 20 && |/wp-json/acms/v1/automation/process-scheduled|30|GET|Process scheduled (+20s)"
        "scrape-monitor|*/2 * * * *|sleep 40 && |/wp-json/acms/v1/cron/scrape-monitor|30|GET|Scrape monitor (+40s)"
        "queue-processor|*/2 * * * *|sleep 60 && |/wp-json/acms-cat/v1/cron/queue-processor|60|GET|Queue processor (+60s)"
        "queue-monitor|*/2 * * * *|sleep 80 && |/wp-json/acms-cat/v1/cron/queue-monitor|30|GET|Queue monitor (+80s)"
        "product-ai|*/5 * * * *||/wp-json/acms/v1/cron/product-ai|30|GET|Product AI"
        "post-ai|*/5 * * * *|sleep 30 && |/wp-json/acms/v1/cron/post-ai|30|GET|Post AI (+30s)"
        "category-ai|*/5 * * * *|sleep 60 && |/wp-json/acms-cat/v1/cron/category-ai|60|GET|Category AI (+60s)"
        "brand-ai|*/5 * * * *|sleep 90 && |/wp-json/acms/v1/cron/brand-ai|30|GET|Brand AI (+90s)"
        "brand-category-ai|*/5 * * * *|sleep 120 && |/wp-json/acms-cat/v1/cron/brand-category-ai|30|GET|Brand category AI (+120s)"
        "quick-update|*/30 * * * *||/wp-json/acms/v1/cron/quick-update|60|GET|Quick update"
        "retry-stuck|*/10 * * * *||/wp-json/acms-cat/v1/cron/retry-stuck|30|GET|Retry stuck"
        "date-update|0 3 * * *||/wp-json/acms/v1/cron/date-update|30|GET|Date update (daily 3am)"
        "bulk-update|* * * * *||/wp-json/acms-cat/v1/cron/bulk-update-worker|90|GET|Bulk update worker"
        "cache-preload|0 3 * * 0||/wp-json/acms/v1/cache/preload|30|GET|Cache preload (weekly)"
        "cache-refresh|0 */4 * * *||/wp-json/acms/v1/cache/smart-refresh|30|POST|Cache refresh (4h)"
        "cache-resume|*/30 * * * *||/wp-json/acms/v1/cache/resume-queue|30|POST|Cache resume queue"
    )

    printf "\n  ${BOLD}%-14s %-30s %s${NC}\n" "Schedule" "Description" "Endpoint"
    printf "  ──────────────────────────────────────────────────────────\n"

    printf "\n  ${DIM}Tier 1: Pipeline (every 2 min, staggered):${NC}\n"
    for p in "${presets[@]}"; do
        IFS='|' read -r name sched sleep_prefix endpoint max_time method desc <<< "$p"
        [[ "$sched" != "*/2"* ]] && continue
        printf "    %-14s %-30s %s\n" "$sched" "$desc" "$endpoint"
    done

    printf "\n  ${DIM}Tier 2: AI Generation (every 5 min, staggered):${NC}\n"
    for p in "${presets[@]}"; do
        IFS='|' read -r name sched sleep_prefix endpoint max_time method desc <<< "$p"
        [[ "$sched" != "*/5"* ]] && continue
        printf "    %-14s %-30s %s\n" "$sched" "$desc" "$endpoint"
    done

    printf "\n  ${DIM}Tier 3: Maintenance (background):${NC}\n"
    for p in "${presets[@]}"; do
        IFS='|' read -r name sched sleep_prefix endpoint max_time method desc <<< "$p"
        [[ "$sched" == "*/2"* || "$sched" == "*/5"* ]] && continue
        printf "    %-14s %-30s %s\n" "$sched" "$desc" "$endpoint"
    done

    printf "\n  Total: %d cron jobs\n" "${#presets[@]}"
    printf "  All use ${GREEN}--resolve localhost${NC} (bypass Cloudflare, direct to server)\n\n"

    confirm "Install all ${#presets[@]} cron jobs?" || return 0

    local count=0
    for p in "${presets[@]}"; do
        IFS='|' read -r name sched sleep_prefix endpoint max_time method desc <<< "$p"
        local cron_file="/etc/cron.d/az-wp-${name}"
        local curl_cmd="${base} --max-time ${max_time}"
        [[ "$method" == "POST" ]] && curl_cmd="${curl_cmd} -X POST"
        printf '# az-wp cron: %s\n%s root %s%s "%s%s?token=%s" > /dev/null 2>&1\n' \
            "$desc" "$sched" "$sleep_prefix" "$curl_cmd" "$url" "$endpoint" "$api_token" > "$cron_file"
        chmod 644 "$cron_file"
        count=$((count + 1))
    done

    log_success "Installed $count AffiliateCMS cron jobs."
    printf "\n"
    _cron_show_all
}

# ===========================================================================
# 7) DOMAIN CHANGE
# ===========================================================================
menu_domain() {
    local sub="${1:-change}"

    _header "Domain Management"
    printf "  Current domain: ${GREEN}%s${NC}\n" "$DOMAIN"
    printf "  Web root:       %s\n" "$WEB_ROOT"
    printf "  Cache path:     %s\n\n" "$CACHE_PATH"

    local new_domain
    read -rp "  New domain: " new_domain
    [[ -z "$new_domain" ]] && { log_warn "No domain entered."; return; }
    [[ "$new_domain" == "$DOMAIN" ]] && { log_warn "Same as current domain."; return; }

    local old_domain="$DOMAIN"
    local old_web_root="$WEB_ROOT"
    local new_web_root="/home/${SITE_USER}/${new_domain}"

    printf "\n  ${BOLD}Changes:${NC}\n"
    printf "    Domain:    %s → %s\n" "$old_domain" "$new_domain"
    printf "    Web root:  %s → %s\n" "$old_web_root" "$new_web_root"
    printf "    Database:  search & replace all URLs\n"
    printf "    Nginx:     new server_name + config file\n"
    printf "    SSL:       new certificate\n"
    printf "    Cron:      update all domain references\n"
    printf "    Cache:     flush all + update paths\n\n"

    confirm "Proceed with domain change?" || return 0

    # === Step 1: Database search & replace ===
    log_sub "1/9 Replacing in database: $old_domain → $new_domain ..."
    wp_run search-replace "$old_domain" "$new_domain" --all-tables --precise > /dev/null
    wp_run search-replace "http://$old_domain" "https://$new_domain" --all-tables --precise > /dev/null

    # === Step 2: WordPress URLs ===
    log_sub "2/9 Updating WordPress site URL..."
    wp_run option update siteurl "https://$new_domain" > /dev/null
    wp_run option update home "https://$new_domain" > /dev/null

    # === Step 3: Rename web root directory ===
    log_sub "3/9 Renaming web root..."
    if [[ "$old_web_root" != "$new_web_root" && -d "$old_web_root" ]]; then
        mv "$old_web_root" "$new_web_root"
        WEB_ROOT="$new_web_root"
    fi

    # === Step 4: Nginx config (re-render from template — safer than sed) ===
    log_sub "4/9 Rebuilding Nginx configuration..."
    local new_conf="/etc/nginx/sites-available/${new_domain}.conf"

    export DOMAIN="$new_domain" WEB_ROOT="$new_web_root" PHP_VERSION
    envsubst '$DOMAIN $WEB_ROOT $PHP_VERSION' < "${AZ_DIR}/templates/nginx/site.conf.tpl" > "$new_conf"

    # Remove old config and symlink
    rm -f "/etc/nginx/sites-available/${old_domain}.conf" 2>/dev/null
    rm -f "/etc/nginx/sites-enabled/${old_domain}.conf" 2>/dev/null
    ln -sf "$new_conf" "/etc/nginx/sites-enabled/${new_domain}.conf"

    # Re-add phpMyAdmin include if exists
    if [[ -f /etc/nginx/az-wp-pma.conf ]]; then
        sed -i '/^}$/i \    include /etc/nginx/az-wp-pma.conf;' "$new_conf" 2>/dev/null || true
    fi

    # Make sure nginx works before SSL
    nginx -t 2>/dev/null && systemctl reload nginx 2>/dev/null

    # === Step 5: SSL certificate ===
    log_sub "5/9 Issuing new SSL certificate..."
    PUBLIC_IP="$(curl -sf --max-time 5 https://ifconfig.me 2>/dev/null)" || PUBLIC_IP=""
    DOMAIN="$new_domain"
    WP_ADMIN_EMAIL="$(state_get WP_ADMIN_EMAIL 2>/dev/null)" || true
    issue_certificate 2>/dev/null || log_warn "SSL issue failed. Run 'az-wp advanced ssl issue' later."

    # === Step 6: Cron jobs ===
    log_sub "6/9 Updating cron jobs..."
    for f in /etc/cron.d/az-wp-*; do
        [[ -f "$f" ]] && sed -i "s|${old_domain}|${new_domain}|g" "$f"
    done
    # Update wp-cron path
    if [[ -f /etc/cron.d/az-wp-cron ]]; then
        sed -i "s|${old_web_root}|${new_web_root}|g" /etc/cron.d/az-wp-cron
    fi

    # === Step 7: Cache ===
    log_sub "7/9 Flushing caches..."
    rm -rf "${CACHE_PATH:?}"/* 2>/dev/null || true
    redis-cli -s "$REDIS_SOCK" FLUSHDB > /dev/null 2>&1 || true
    wp_run config set WP_CACHE_KEY_SALT "${new_domain}_" > /dev/null

    # Regenerate cache-stats.json with real data
    _refresh_cache_stats

    # Update cache-stats cron if exists
    if [[ -f /etc/cron.d/az-wp-cache-stats ]]; then
        sed -i "s|${old_web_root}|${new_web_root}|g" /etc/cron.d/az-wp-cache-stats 2>/dev/null || true
    fi

    # === Step 8: Update WordPress config ===
    log_sub "8/9 Updating wp-config.php..."
    # Update open_basedir path in PHP ini (if web root changed)
    if [[ "$old_web_root" != "$new_web_root" ]]; then
        local php_ini="/etc/php/${PHP_VERSION}/fpm/conf.d/99-az-wp.ini"
        if [[ -f "$php_ini" ]]; then
            sed -i "s|${old_web_root}|${new_web_root}|g" "$php_ini" 2>/dev/null || true
        fi
        systemctl restart "php${PHP_VERSION}-fpm" 2>/dev/null
    fi

    # Update plugin cache_path setting
    wp_run option patch update acms_general_settings cache_path "${CACHE_PATH}/" > /dev/null 2>/dev/null || true

    # === Step 9: State file ===
    log_sub "9/9 Updating state file..."
    state_set "DOMAIN" "$new_domain"
    state_set "WEB_ROOT" "$new_web_root"
    DOMAIN="$new_domain"
    WEB_ROOT="$new_web_root"

    # Reload nginx
    if nginx -t 2>/dev/null; then
        systemctl reload nginx
    else
        log_warn "Nginx config error. Run: nginx -t"
    fi

    # ==========================================================
    # VERIFICATION — check everything updated correctly
    # ==========================================================
    printf "\n"
    log_success "Domain changed: $old_domain → $new_domain"
    printf "\n${BOLD}  Verification:${NC}\n"
    printf "  ──────────────────────────────────────\n"

    # Website
    local http_code
    http_code="$(curl -sfk --resolve "${new_domain}:443:127.0.0.1" -o /dev/null -w '%{http_code}' "https://${new_domain}/" 2>/dev/null)" || http_code="000"
    if [[ "$http_code" == "200" ]]; then
        printf "  ${GREEN}OK${NC}  Website:     https://%s (HTTP %s)\n" "$new_domain" "$http_code"
    else
        printf "  ${RED}!!${NC}  Website:     https://%s (HTTP %s)\n" "$new_domain" "$http_code"
    fi

    # WP Admin
    local admin_code
    admin_code="$(curl -sfk --resolve "${new_domain}:443:127.0.0.1" -o /dev/null -w '%{http_code}' "https://${new_domain}/wp-admin/" 2>/dev/null)" || admin_code="000"
    if [[ "$admin_code" == "302" || "$admin_code" == "200" ]]; then
        printf "  ${GREEN}OK${NC}  WP Admin:    https://%s/wp-admin (HTTP %s)\n" "$new_domain" "$admin_code"
    else
        printf "  ${RED}!!${NC}  WP Admin:    https://%s/wp-admin (HTTP %s)\n" "$new_domain" "$admin_code"
    fi

    # WordPress URL
    local wp_url
    wp_url="$(wp_run option get siteurl 2>/dev/null | tr -d '[:space:]')" || wp_url="?"
    if [[ "$wp_url" == "https://${new_domain}" ]]; then
        printf "  ${GREEN}OK${NC}  WP siteurl:  %s\n" "$wp_url"
    else
        printf "  ${RED}!!${NC}  WP siteurl:  %s (expected https://%s)\n" "$wp_url" "$new_domain"
    fi

    # Web root
    if [[ -d "$new_web_root" ]]; then
        printf "  ${GREEN}OK${NC}  Web root:    %s\n" "$new_web_root"
    else
        printf "  ${RED}!!${NC}  Web root:    %s (NOT FOUND)\n" "$new_web_root"
    fi

    # SSL
    local ssl_issuer
    ssl_issuer="$(echo | openssl s_client -connect localhost:443 -servername "$new_domain" 2>/dev/null | openssl x509 -noout -issuer 2>/dev/null | cut -d= -f3)" || ssl_issuer=""
    if [[ -n "$ssl_issuer" ]]; then
        printf "  ${GREEN}OK${NC}  SSL:         %s\n" "$ssl_issuer"
    else
        printf "  ${YELLOW}--${NC}  SSL:         not issued (run: az-wp advanced ssl issue)\n"
    fi

    # Nginx config
    if nginx -t 2>/dev/null; then
        printf "  ${GREEN}OK${NC}  Nginx:       config valid\n"
    else
        printf "  ${RED}!!${NC}  Nginx:       config ERROR\n"
    fi

    # Crons — check no old domain refs
    local old_refs
    old_refs="$(grep -rl "$old_domain" /etc/cron.d/az-wp-* 2>/dev/null | wc -l)" || old_refs=0
    local cron_count
    cron_count="$(ls /etc/cron.d/az-wp-* 2>/dev/null | wc -l)" || cron_count=0
    if [[ "$old_refs" -eq 0 ]]; then
        printf "  ${GREEN}OK${NC}  Crons:       %s jobs, all updated\n" "$cron_count"
    else
        printf "  ${RED}!!${NC}  Crons:       %s files still reference %s\n" "$old_refs" "$old_domain"
    fi

    # Cache path
    local cache_setting
    cache_setting="$(wp_run option pluck acms_general_settings cache_path 2>/dev/null | tr -d '[:space:]')" || cache_setting="?"
    printf "  ${GREEN}OK${NC}  Cache path:  %s\n" "${cache_setting:-$CACHE_PATH/}"

    # Cache stats
    local stats_file="/home/${SITE_USER}/cache/cache-stats.json"
    if [[ -f "$stats_file" ]]; then
        local sf_count sf_size
        sf_count="$(python3 -c "import json; print(json.load(open('$stats_file'))['files'])" 2>/dev/null)" || sf_count="?"
        sf_size="$(python3 -c "import json; s=json.load(open('$stats_file'))['size']; print(f'{s/1048576:.1f} MB')" 2>/dev/null)" || sf_size="?"
        printf "  ${GREEN}OK${NC}  Cache stats: %s files, %s\n" "$sf_count" "$sf_size"
    else
        printf "  ${YELLOW}--${NC}  Cache stats: not generated yet\n"
    fi

    # State file
    local state_domain state_webroot
    state_domain="$(state_get DOMAIN 2>/dev/null)" || state_domain="?"
    state_webroot="$(state_get WEB_ROOT 2>/dev/null)" || state_webroot="?"
    if [[ "$state_domain" == "$new_domain" && "$state_webroot" == "$new_web_root" ]]; then
        printf "  ${GREEN}OK${NC}  State file:  DOMAIN=%s, WEB_ROOT updated\n" "$state_domain"
    else
        printf "  ${RED}!!${NC}  State file:  DOMAIN=%s, WEB_ROOT=%s\n" "$state_domain" "$state_webroot"
    fi

    # Redis
    local redis_salt
    redis_salt="$(wp_run config get WP_CACHE_KEY_SALT 2>/dev/null | tr -d '[:space:]')" || redis_salt="?"
    printf "  ${GREEN}OK${NC}  Redis salt:  %s\n" "$redis_salt"

    printf "  ──────────────────────────────────────\n\n"
}

