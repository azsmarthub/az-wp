# Get cron filename by number
_cron_get_by_number() {
    local num="$1"
    local i=0
    for f in /etc/cron.d/azwp-*; do
        [[ ! -f "$f" ]] && continue
        [[ "$(basename "$f")" == "azwp-cron" ]] && continue
        i=$((i + 1))
        if [[ $i -eq $num ]]; then
            basename "$f"
            return 0
        fi
    done
    return 1
}

# Add a single cron from a raw line
_cron_add_line() {
    local cron_line="$1"

    # Auto-generate name from URL path
    local cron_name
    cron_name="$(printf '%s' "$cron_line" | grep -oP '/v1/[^?"]+' | sed 's|/v1/||;s|/|-|g' | head -1 || true)"
    [[ -z "$cron_name" ]] && cron_name="custom-$(openssl rand -hex 3)"

    # Extract schedule (first 5 fields) and command (rest)
    local sched cmd
    sched="$(printf '%s' "$cron_line" | awk '{print $1,$2,$3,$4,$5}')"
    cmd="$(printf '%s' "$cron_line" | awk '{for(i=6;i<=NF;i++) printf "%s ", $i}')"
    cmd="${cmd% }"

    # Strip existing redirect (will re-add at the end)
    cmd="$(printf '%s' "$cmd" | sed 's|>>* */dev/null[^"]*||; s|2>&1||g')"
    cmd="${cmd% }"

    # Auto-add --resolve if curl without it (loopback optimization)
    if [[ "$cmd" == *curl* && "$cmd" != *--resolve* && "$cmd" == *"${DOMAIN}"* ]]; then
        cmd="$(printf '%s' "$cmd" | sed "s|curl |curl -sk --resolve ${DOMAIN}:443:127.0.0.1 --max-time 30 |")"
    fi

    # Always add clean redirect
    cmd="$cmd > /dev/null 2>&1"

    local cron_file="/etc/cron.d/azwp-${cron_name}"
    printf '# azwp cron: %s\n%s root %s\n' \
        "$cron_name" "$sched" "$cmd" > "$cron_file"
    chmod 644 "$cron_file"
    log_success "Created: $cron_file"
}

# Install AffiliateCMS preset (15 cron jobs)
_cron_install_preset() {
    _header "AffiliateCMS Cron Preset"

    # Get API token
    local api_token
    api_token="$(wp_run option get acms_api_token 2>/dev/null | tr -d '[:space:]')" || api_token=""

    if [[ -z "$api_token" ]]; then
        printf "  ${YELLOW}AffiliateCMS API token not found.${NC}\n"
        read -rp "  Enter API token: " api_token
        [[ -z "$api_token" ]] && { log_warn "Token required. Skipped."; return 0; }
    else
        printf "  API token: ${GREEN}found${NC}\n"
    fi

    # Loopback curl command (bypasses Cloudflare, connects to localhost)
    local base="curl -sk --resolve ${DOMAIN}:443:127.0.0.1"
    local url="https://${DOMAIN}"

    # All preset crons: name|schedule|endpoint|max_time|method|description
    # Tier 1: Pipeline (every 2 min, staggered with sleep offsets)
    # Tier 2: AI Generation (every 5 min, staggered)
    # Tier 3: Maintenance (background — less frequent)
    #
    # Stagger pattern: related crons offset by 20-30s to avoid race conditions
    # scrape(0s) → process-scheduled(20s) → scrape-monitor(40s) → queue-processor(60s) → queue-monitor(80s)
    # product-ai(0s) → post-ai(30s) → category-ai(60s) → brand-ai(90s) → brand-category-ai(120s)
    local presets=(
        "scrape|*/2 * * * *||/wp-json/acms/v1/automation/scrape|30|GET|Scrape dispatcher"
        "process-scheduled|*/2 * * * *|sleep 20 && |/wp-json/acms/v1/automation/process-scheduled|30|GET|Process scheduled (+20s)"
        "scrape-monitor|*/2 * * * *|sleep 40 && |/wp-json/acms/v1/cron/scrape-monitor|30|GET|Scrape monitor (+40s)"
        "pro-queue|*/2 * * * *|sleep 50 && |/wp-json/acms/v1/cron/queue|60|GET|Pro keyword queue (+50s)"
        "queue-processor|*/2 * * * *|sleep 60 && |/wp-json/acms-cat/v1/cron/queue-processor|60|GET|Queue processor (+60s)"
        "queue-monitor|*/2 * * * *|sleep 80 && |/wp-json/acms-cat/v1/cron/queue-monitor|30|GET|Queue monitor (+80s)"
        "product-ai|*/5 * * * *||/wp-json/acms/v1/cron/product-ai|30|GET|Product AI"
        "post-ai|*/5 * * * *|sleep 30 && |/wp-json/acms/v1/cron/post-ai|30|GET|Post AI (+30s)"
        "category-ai|*/5 * * * *|sleep 60 && |/wp-json/acms-cat/v1/cron/category-ai|60|GET|Category AI (+60s)"
        "ai-jobs|*/2 * * * *|sleep 10 && |/wp-json/acms-ai/v1/cron/process-jobs|300|GET|AI job processor (+10s)"
        "brand-ai|*/5 * * * *|sleep 90 && |/wp-json/acms/v1/cron/brand-ai|30|GET|Brand AI (+90s)"
        "brand-category-ai|*/5 * * * *|sleep 120 && |/wp-json/acms-cat/v1/cron/brand-category-ai|30|GET|Brand category AI (+120s)"
        "quick-update|*/30 * * * *||/wp-json/acms/v1/cron/quick-update|60|GET|Quick update"
        "retry-stuck|*/10 * * * *||/wp-json/acms-cat/v1/cron/retry-stuck|30|GET|Retry stuck"
        "date-update|0 3 * * *||/wp-json/acms/v1/cron/date-update|30|GET|Date update (daily 3am)"
        "bulk-update|* * * * *||/wp-json/acms-cat/v1/cron/bulk-update-worker|90|GET|Bulk update worker"
        "cache-preload|0 3 * * 0||/wp-json/acms/v1/cache/preload|30|GET|Cache preload (weekly)"
        "cache-refresh|0 */4 * * *||/wp-json/acms/v1/cache/smart-refresh|30|POST|Cache refresh (4h)"
        "cache-resume|*/30 * * * *||/wp-json/acms/v1/cache/resume-queue|30|POST|Cache resume queue"
    )

    printf "\n  ${BOLD}%-14s %-30s %s${NC}\n" "Schedule" "Description" "Endpoint"
    printf "  ──────────────────────────────────────────────────────────\n"

    printf "\n  ${DIM}Tier 1: Pipeline (every 2 min, staggered):${NC}\n"
    for p in "${presets[@]}"; do
        IFS='|' read -r name sched sleep_prefix endpoint max_time method desc <<< "$p"
        [[ "$sched" != "*/2"* ]] && continue
        printf "    %-14s %-30s %s\n" "$sched" "$desc" "$endpoint"
    done

    printf "\n  ${DIM}Tier 2: AI Generation (every 5 min, staggered):${NC}\n"
    for p in "${presets[@]}"; do
        IFS='|' read -r name sched sleep_prefix endpoint max_time method desc <<< "$p"
        [[ "$sched" != "*/5"* ]] && continue
        printf "    %-14s %-30s %s\n" "$sched" "$desc" "$endpoint"
    done

    printf "\n  ${DIM}Tier 3: Maintenance (background):${NC}\n"
    for p in "${presets[@]}"; do
        IFS='|' read -r name sched sleep_prefix endpoint max_time method desc <<< "$p"
        [[ "$sched" == "*/2"* || "$sched" == "*/5"* ]] && continue
        printf "    %-14s %-30s %s\n" "$sched" "$desc" "$endpoint"
    done

    printf "\n  Total: %d cron jobs\n" "${#presets[@]}"
    printf "  All use ${GREEN}--resolve localhost${NC} (bypass Cloudflare, direct to server)\n\n"

    confirm "Install all ${#presets[@]} cron jobs?" || return 0

    local count=0
    for p in "${presets[@]}"; do
        IFS='|' read -r name sched sleep_prefix endpoint max_time method desc <<< "$p"
        local cron_file="/etc/cron.d/azwp-${name}"
        local curl_cmd="${base} --max-time ${max_time}"
        [[ "$method" == "POST" ]] && curl_cmd="${curl_cmd} -X POST"
        printf '# azwp cron: %s\n%s root %s%s "%s%s?token=%s" > /dev/null 2>&1\n' \
            "$desc" "$sched" "$sleep_prefix" "$curl_cmd" "$url" "$endpoint" "$api_token" > "$cron_file"
        chmod 644 "$cron_file"
        count=$((count + 1))
    done

    log_success "Installed $count AffiliateCMS cron jobs."
    printf "\n"
    _cron_show_all
}

# ===========================================================================
# 7) DOMAIN CHANGE
# ===========================================================================
menu_domain() {
    local sub="${1:-change}"

    _header "Domain Management"
    printf "  Current domain: ${GREEN}%s${NC}\n" "$DOMAIN"
    printf "  Web root:       %s\n" "$WEB_ROOT"
    printf "  Cache path:     %s\n\n" "$CACHE_PATH"

    local new_domain
    read -rp "  New domain: " new_domain
    [[ -z "$new_domain" ]] && { log_warn "No domain entered."; return; }
    [[ "$new_domain" == "$DOMAIN" ]] && { log_warn "Same as current domain."; return; }

    local old_domain="$DOMAIN"
    local old_user="$SITE_USER"
    local old_web_root="$WEB_ROOT"
    local old_home="/home/${old_user}"

    # Derive new user from domain (same logic as install.sh)
    local new_user
    new_user="$(echo "$new_domain" | tr '.' '_' | tr '-' '_')"
    local new_home="/home/${new_user}"
    local new_web_root="${new_home}/${new_domain}"
    local new_cache="/home/${new_user}/cache/fastcgi"

    printf "\n  ${BOLD}Changes:${NC}\n"
    printf "    Domain:      %s → %s\n" "$old_domain" "$new_domain"
    printf "    User:        %s → %s\n" "$old_user" "$new_user"
    printf "    Home:        %s → %s\n" "$old_home" "$new_home"
    printf "    Web root:    %s → %s\n" "$old_web_root" "$new_web_root"
    printf "    Database:    search & replace all URLs\n"
    printf "    MySQL user:  rename + regrant\n"
    printf "    Nginx:       re-render from template\n"
    printf "    PHP-FPM:     re-render pool configs\n"
    printf "    SSL:         new certificate\n"
    printf "    Cron:        update all references\n"
    printf "    Cache:       flush all + update paths\n"
    printf "    Security:    update scanner\n"
    printf "    Cloudflare:  update cache rule (if configured)\n\n"

    confirm "Proceed with domain change?" || return 0

    # === Step 1: Database search & replace ===
    log_sub "1/14 Replacing in database: $old_domain → $new_domain ..."
    wp_run search-replace "$old_domain" "$new_domain" --all-tables --precise > /dev/null
    wp_run search-replace "http://$old_domain" "https://$new_domain" --all-tables --precise > /dev/null
    # Replace old user path in DB (serialized options like cache_path)
    if [[ "$old_user" != "$new_user" ]]; then
        wp_run search-replace "/home/${old_user}" "/home/${new_user}" --all-tables --precise > /dev/null 2>/dev/null || true
    fi
    wp_run cache flush > /dev/null 2>/dev/null || true

    # === Step 2: WordPress URLs + admin email ===
    log_sub "2/14 Updating WordPress site URL..."
    wp_run option update siteurl "https://$new_domain" > /dev/null
    wp_run option update home "https://$new_domain" > /dev/null

    # Update admin email if it matches the old domain pattern (admin@old_domain)
    local current_email
    current_email="$(wp_run option get admin_email 2>/dev/null | tr -d '[:space:]')" || current_email=""
    if [[ "$current_email" == "admin@${old_domain}" ]]; then
        wp_run option update admin_email "admin@${new_domain}" > /dev/null
        state_set "WP_ADMIN_EMAIL" "admin@${new_domain}"
    fi

    # === Step 3: Rename Linux user + home directory ===
    log_sub "3/14 Renaming system user: $old_user → $new_user ..."
    if [[ "$old_user" != "$new_user" ]]; then
        # Stop services that hold files open
        systemctl stop "php${PHP_VERSION}-fpm" 2>/dev/null || true

        # Rename group first, then user
        groupmod -n "$new_user" "$old_user" 2>/dev/null || true
        usermod -l "$new_user" -d "$new_home" -m "$old_user" 2>/dev/null || true

        # Rename web root inside new home
        if [[ -d "${new_home}/${old_domain}" ]]; then
            mv "${new_home}/${old_domain}" "${new_home}/${new_domain}"
        fi

        # Update cache dir path
        mkdir -p "$new_cache"
        chown "${new_user}:${new_user}" "$new_cache"

        # Re-add to redis group
        usermod -aG redis "$new_user" 2>/dev/null || true

        SITE_USER="$new_user"
        WEB_ROOT="$new_web_root"
        CACHE_PATH="$new_cache"
    else
        # Same user, just rename web root dir
        if [[ "$old_web_root" != "$new_web_root" && -d "$old_web_root" ]]; then
            mv "$old_web_root" "$new_web_root"
        fi
        WEB_ROOT="$new_web_root"
    fi

    # === Step 4: MySQL user + database rename ===
    log_sub "4/14 Renaming MySQL user + database..."
    local old_db_user new_db_user old_db_name new_db_name db_pass
    old_db_user="$(state_get DB_USER 2>/dev/null)" || old_db_user=""
    old_db_name="$(state_get DB_NAME 2>/dev/null)" || old_db_name=""
    db_pass="$(state_get DB_PASS 2>/dev/null)" || db_pass=""
    new_db_user="wp_$(echo "$new_user" | cut -c1-11)"
    new_db_name="$new_db_user"

    if [[ -n "$old_db_user" && "$old_db_user" != "$new_db_user" && -n "$db_pass" ]]; then
        # Rename user
        mysql -e "RENAME USER '${old_db_user}'@'localhost' TO '${new_db_user}'@'localhost';" 2>/dev/null || true

        # Rename database (MySQL has no RENAME DATABASE — move tables)
        if [[ -n "$old_db_name" && "$old_db_name" != "$new_db_name" ]]; then
            mysql -e "CREATE DATABASE IF NOT EXISTS \`${new_db_name}\`;" 2>/dev/null || true
            local _tables
            _tables="$(mysql -N -e "SHOW TABLES FROM \`${old_db_name}\`" 2>/dev/null)" || _tables=""
            for _t in $_tables; do
                mysql -e "RENAME TABLE \`${old_db_name}\`.\`${_t}\` TO \`${new_db_name}\`.\`${_t}\`;" 2>/dev/null || true
            done
            mysql -e "DROP DATABASE IF EXISTS \`${old_db_name}\`;" 2>/dev/null || true
        fi

        # Update grants
        mysql -e "GRANT ALL PRIVILEGES ON \`${new_db_name}\`.* TO '${new_db_user}'@'localhost';" 2>/dev/null || true
        mysql -e "FLUSH PRIVILEGES;" 2>/dev/null || true

        # Update wp-config.php
        wp_run config set DB_NAME "$new_db_name" --type=constant > /dev/null 2>/dev/null || true
        wp_run config set DB_USER "$new_db_user" --type=constant > /dev/null 2>/dev/null || true
        state_set "DB_NAME" "$new_db_name"
        state_set "DB_USER" "$new_db_user"
    fi

    # === Step 5: PHP-FPM pool configs ===
    log_sub "5/14 Updating PHP-FPM pools..."
    local pool_dir="/etc/php/${PHP_VERSION}/fpm/pool.d"
    for pool_file in "$pool_dir/web.conf" "$pool_dir/workers.conf"; do
        if [[ -f "$pool_file" ]]; then
            sed -i "s|${old_user}|${new_user}|g" "$pool_file" 2>/dev/null || true
        fi
    done

    # Sudoers for nginx-cache-purge
    if [[ -f /etc/sudoers.d/nginx-cache-purge ]]; then
        sed -i "s|${old_user}|${new_user}|g" /etc/sudoers.d/nginx-cache-purge 2>/dev/null || true
    fi

    # FastCGI cache zone config (references cache path)
    if [[ -f /etc/nginx/conf.d/fastcgi-cache.conf ]]; then
        sed -i "s|/home/${old_user}|/home/${new_user}|g" /etc/nginx/conf.d/fastcgi-cache.conf 2>/dev/null || true
    fi

    # open_basedir
    local php_ini="/etc/php/${PHP_VERSION}/fpm/conf.d/99-azwp.ini"
    if [[ -f "$php_ini" ]]; then
        sed -i "s|/home/${old_user}|/home/${new_user}|g" "$php_ini" 2>/dev/null || true
    fi

    # === Step 6: Nginx config (re-render from template) ===
    log_sub "6/14 Rebuilding Nginx configuration..."
    local new_conf="/etc/nginx/sites-available/${new_domain}.conf"

    export DOMAIN="$new_domain" WEB_ROOT="$new_web_root" PHP_VERSION
    envsubst '$DOMAIN $WEB_ROOT $PHP_VERSION' < "${AZ_DIR}/templates/nginx/site.conf.tpl" > "$new_conf"

    rm -f "/etc/nginx/sites-available/${old_domain}.conf" 2>/dev/null
    rm -f "/etc/nginx/sites-enabled/${old_domain}.conf" 2>/dev/null
    ln -sf "$new_conf" "/etc/nginx/sites-enabled/${new_domain}.conf"

    # Re-add phpMyAdmin include if exists
    if [[ -f /etc/nginx/azwp-pma.conf ]]; then
        sed -i '/^}$/i \    include /etc/nginx/azwp-pma.conf;' "$new_conf" 2>/dev/null || true
    fi

    nginx -t 2>/dev/null && systemctl reload nginx 2>/dev/null

    # === Step 7: SSL certificate ===
    log_sub "7/14 Issuing new SSL certificate..."
    PUBLIC_IP="$(curl -sf --max-time 5 https://ifconfig.me 2>/dev/null)" || PUBLIC_IP=""
    DOMAIN="$new_domain"
    WP_ADMIN_EMAIL="$(state_get WP_ADMIN_EMAIL 2>/dev/null)" || true
    issue_certificate 2>/dev/null || log_warn "SSL issue failed. Run 'azwp advanced ssl issue' later."

    # === Step 8: Cron jobs ===
    log_sub "8/14 Updating cron jobs..."
    for f in /etc/cron.d/azwp-*; do
        if [[ -f "$f" ]]; then
            sed -i "s|${old_domain}|${new_domain}|g" "$f"
            sed -i "s|/home/${old_user}|/home/${new_user}|g" "$f"
            sed -i "s| ${old_user} | ${new_user} |g" "$f"
        fi
    done

    # === Step 9: Cache ===
    log_sub "9/14 Flushing caches..."
    rm -rf "${new_cache:?}"/* 2>/dev/null || true
    redis-cli -s "$REDIS_SOCK" FLUSHDB > /dev/null 2>&1 || true
    wp_run config set WP_CACHE_KEY_SALT "${new_domain}_" > /dev/null

    # Clear preload queue
    rm -f "${new_web_root}/wp-content/uploads/acms-preload-queue.txt" 2>/dev/null || true
    wp_run option delete acms_preload_meta > /dev/null 2>/dev/null || true

    _refresh_cache_stats

    if [[ -f /etc/cron.d/azwp-cache-stats ]]; then
        sed -i "s|/home/${old_user}|/home/${new_user}|g" /etc/cron.d/azwp-cache-stats 2>/dev/null || true
    fi

    # === Step 10: WordPress config ===
    log_sub "10/14 Updating WordPress config..."
    # Restart FPM (clears OPcache + applies new pool configs)
    systemctl restart "php${PHP_VERSION}-fpm" 2>/dev/null

    # Update cache_path in serialized option (wp option patch may fail on serialized data)
    wp_run eval "\$s = get_option('acms_general_settings', []); \$s['cache_path'] = '${new_cache}/'; update_option('acms_general_settings', \$s);" > /dev/null 2>/dev/null || true
    # Flush object cache + transients (Redis may cache stale serialized data)
    wp_run transient delete --all > /dev/null 2>/dev/null || true
    wp_run cache flush > /dev/null 2>/dev/null || true
    redis-cli -s "$REDIS_SOCK" FLUSHDB > /dev/null 2>&1 || true

    # === Step 11: Security scanner ===
    log_sub "11/14 Updating security scanner..."
    if [[ -x /usr/local/bin/azwp-security-scan ]]; then
        sed -i "s|${old_domain}|${new_domain}|g" /usr/local/bin/azwp-security-scan 2>/dev/null || true
        sed -i "s|/home/${old_user}|/home/${new_user}|g" /usr/local/bin/azwp-security-scan 2>/dev/null || true
    fi

    # === Step 12: Cloudflare cache rule ===
    log_sub "12/14 Updating Cloudflare cache rule..."
    local cf_zone cf_email cf_key
    cf_email="$(config_get CF_EMAIL 2>/dev/null)" || cf_email=""
    cf_key="$(config_get CF_API_KEY 2>/dev/null)" || cf_key=""
    cf_zone="$(config_get CF_ZONE_ID 2>/dev/null)" || cf_zone=""
    if [[ -n "$cf_zone" && -n "$cf_email" && -n "$cf_key" ]]; then
        local cf_expr="(http.host eq \\\"${new_domain}\\\" and not starts_with(http.request.uri.path, \\\"/wp-admin\\\") and not starts_with(http.request.uri.path, \\\"/wp-login\\\") and not starts_with(http.request.uri.path, \\\"/wp-json\\\") and http.request.method eq \\\"GET\\\" and not http.cookie contains \\\"wordpress_logged_in\\\")"
        local rule_response
        rule_response="$(curl -sf -X PUT \
            "https://api.cloudflare.com/client/v4/zones/${cf_zone}/rulesets/phases/http_request_cache_settings/entrypoint" \
            -H "X-Auth-Email: ${cf_email}" -H "X-Auth-Key: ${cf_key}" \
            -H "Content-Type: application/json" \
            -d "{\"rules\":[{\"expression\":\"${cf_expr}\",\"description\":\"azwp-cache: Public pages for ${new_domain}\",\"action\":\"set_cache_settings\",\"action_parameters\":{\"cache\":true,\"edge_ttl\":{\"mode\":\"override_origin\",\"default\":86400},\"browser_ttl\":{\"mode\":\"override_origin\",\"default\":600}}}]}" 2>/dev/null)" || true
        if echo "$rule_response" | grep -q '"success":true\|"success": true'; then
            log_success "Cloudflare cache rule updated for $new_domain"
            curl -sf -X POST "https://api.cloudflare.com/client/v4/zones/${cf_zone}/purge_cache" \
                -H "X-Auth-Email: ${cf_email}" -H "X-Auth-Key: ${cf_key}" \
                -H "Content-Type: application/json" \
                -d '{"purge_everything":true}' > /dev/null 2>&1 || true
        else
            log_warn "Cloudflare update failed. Run 'azwp advanced cloudflare' to reconfigure."
        fi
    else
        printf "    ${DIM}No Cloudflare credentials saved — skipped${NC}\n"
    fi

    # === Step 13: Cleanup ===
    log_sub "13/14 Cleaning up..."
    rm -f "/var/log/nginx/${old_domain}-error.log" 2>/dev/null || true
    # Remove old home if empty (user was moved)
    [[ "$old_home" != "$new_home" && -d "$old_home" ]] && rmdir "$old_home" 2>/dev/null || true

    # === Step 14: State file ===
    log_sub "14/14 Updating state file..."
    state_set "DOMAIN" "$new_domain"
    state_set "SITE_USER" "$new_user"
    state_set "WEB_ROOT" "$new_web_root"
    state_set "CACHE_PATH" "$new_cache"
    DOMAIN="$new_domain"
    SITE_USER="$new_user"
    WEB_ROOT="$new_web_root"
    CACHE_PATH="$new_cache"

    # Reload nginx
    if nginx -t 2>/dev/null; then
        systemctl reload nginx
    else
        log_warn "Nginx config error. Run: nginx -t"
    fi

    # ==========================================================
    # VERIFICATION — check everything updated correctly
    # ==========================================================
    printf "\n"
    log_success "Domain changed: $old_domain → $new_domain"
    printf "\n${BOLD}  Verification:${NC}\n"
    printf "  ──────────────────────────────────────\n"

    # Website
    local http_code
    http_code="$(curl -sfk --resolve "${new_domain}:443:127.0.0.1" -o /dev/null -w '%{http_code}' "https://${new_domain}/" 2>/dev/null)" || http_code="000"
    if [[ "$http_code" == "200" ]]; then
        printf "  ${GREEN}OK${NC}  Website:     https://%s (HTTP %s)\n" "$new_domain" "$http_code"
    else
        printf "  ${RED}!!${NC}  Website:     https://%s (HTTP %s)\n" "$new_domain" "$http_code"
    fi

    # WP Admin
    local admin_code
    admin_code="$(curl -sfk --resolve "${new_domain}:443:127.0.0.1" -o /dev/null -w '%{http_code}' "https://${new_domain}/wp-admin/" 2>/dev/null)" || admin_code="000"
    if [[ "$admin_code" == "302" || "$admin_code" == "200" ]]; then
        printf "  ${GREEN}OK${NC}  WP Admin:    https://%s/wp-admin (HTTP %s)\n" "$new_domain" "$admin_code"
    else
        printf "  ${RED}!!${NC}  WP Admin:    https://%s/wp-admin (HTTP %s)\n" "$new_domain" "$admin_code"
    fi

    # WordPress URL
    local wp_url
    wp_url="$(wp_run option get siteurl 2>/dev/null | tr -d '[:space:]')" || wp_url="?"
    if [[ "$wp_url" == "https://${new_domain}" ]]; then
        printf "  ${GREEN}OK${NC}  WP siteurl:  %s\n" "$wp_url"
    else
        printf "  ${RED}!!${NC}  WP siteurl:  %s (expected https://%s)\n" "$wp_url" "$new_domain"
    fi

    # Web root
    if [[ -d "$new_web_root" ]]; then
        printf "  ${GREEN}OK${NC}  Web root:    %s\n" "$new_web_root"
    else
        printf "  ${RED}!!${NC}  Web root:    %s (NOT FOUND)\n" "$new_web_root"
    fi

    # System user
    if id "$new_user" &>/dev/null; then
        printf "  ${GREEN}OK${NC}  User:        %s (uid=%s)\n" "$new_user" "$(id -u "$new_user")"
    else
        printf "  ${RED}!!${NC}  User:        %s NOT FOUND\n" "$new_user"
    fi

    # MySQL user
    if [[ -n "${new_db_user:-}" ]]; then
        if mysql -e "SELECT 1" -u "$new_db_user" -p"${db_pass}" "${new_db_name}" &>/dev/null; then
            printf "  ${GREEN}OK${NC}  MySQL user:  %s\n" "$new_db_user"
        else
            printf "  ${RED}!!${NC}  MySQL user:  %s (login failed)\n" "$new_db_user"
        fi
    fi

    # SSL
    local ssl_issuer
    ssl_issuer="$(echo | openssl s_client -connect localhost:443 -servername "$new_domain" 2>/dev/null | openssl x509 -noout -issuer 2>/dev/null | cut -d= -f3)" || ssl_issuer=""
    if [[ -n "$ssl_issuer" ]]; then
        printf "  ${GREEN}OK${NC}  SSL:         %s\n" "$ssl_issuer"
    else
        printf "  ${YELLOW}--${NC}  SSL:         not issued (run: azwp advanced ssl issue)\n"
    fi

    # Nginx config
    if nginx -t 2>/dev/null; then
        printf "  ${GREEN}OK${NC}  Nginx:       config valid\n"
    else
        printf "  ${RED}!!${NC}  Nginx:       config ERROR\n"
    fi

    # Crons — check no old domain refs
    local old_refs
    old_refs="$(grep -rl "$old_domain" /etc/cron.d/azwp-* 2>/dev/null | wc -l)" || old_refs=0
    local cron_count
    cron_count="$(ls /etc/cron.d/azwp-* 2>/dev/null | wc -l)" || cron_count=0
    if [[ "$old_refs" -eq 0 ]]; then
        printf "  ${GREEN}OK${NC}  Crons:       %s jobs, all updated\n" "$cron_count"
    else
        printf "  ${RED}!!${NC}  Crons:       %s files still reference %s\n" "$old_refs" "$old_domain"
    fi

    # Cache path
    local cache_setting
    cache_setting="$(wp_run option pluck acms_general_settings cache_path 2>/dev/null | tr -d '[:space:]')" || cache_setting="?"
    printf "  ${GREEN}OK${NC}  Cache path:  %s\n" "${cache_setting:-$CACHE_PATH/}"

    # Cache stats
    local stats_file="/home/${SITE_USER}/cache/cache-stats.json"
    if [[ -f "$stats_file" ]]; then
        local sf_count sf_size
        sf_count="$(python3 -c "import json; print(json.load(open('$stats_file'))['files'])" 2>/dev/null)" || sf_count="?"
        sf_size="$(python3 -c "import json; s=json.load(open('$stats_file'))['size']; print(f'{s/1048576:.1f} MB')" 2>/dev/null)" || sf_size="?"
        printf "  ${GREEN}OK${NC}  Cache stats: %s files, %s\n" "$sf_count" "$sf_size"
    else
        printf "  ${YELLOW}--${NC}  Cache stats: not generated yet\n"
    fi

    # State file
    local state_domain state_webroot
    state_domain="$(state_get DOMAIN 2>/dev/null)" || state_domain="?"
    state_webroot="$(state_get WEB_ROOT 2>/dev/null)" || state_webroot="?"
    if [[ "$state_domain" == "$new_domain" && "$state_webroot" == "$new_web_root" ]]; then
        printf "  ${GREEN}OK${NC}  State file:  DOMAIN=%s, WEB_ROOT updated\n" "$state_domain"
    else
        printf "  ${RED}!!${NC}  State file:  DOMAIN=%s, WEB_ROOT=%s\n" "$state_domain" "$state_webroot"
    fi

    # Redis
    local redis_salt
    redis_salt="$(wp_run config get WP_CACHE_KEY_SALT 2>/dev/null | tr -d '[:space:]')" || redis_salt="?"
    printf "  ${GREEN}OK${NC}  Redis salt:  %s\n" "$redis_salt"

    # Security scanner
    if [[ -x /usr/local/bin/azwp-security-scan ]]; then
        if grep -q "$new_domain" /usr/local/bin/azwp-security-scan 2>/dev/null; then
            printf "  ${GREEN}OK${NC}  Scanner:     domain updated\n"
        else
            printf "  ${RED}!!${NC}  Scanner:     still references old domain\n"
        fi
    else
        printf "  ${DIM}--${NC}  Scanner:     not installed\n"
    fi

    # Cloudflare
    local v_cf_zone
    v_cf_zone="$(config_get CF_ZONE_ID 2>/dev/null)" || v_cf_zone=""
    if [[ -n "$v_cf_zone" ]]; then
        local v_cf_email v_cf_key
        v_cf_email="$(config_get CF_EMAIL 2>/dev/null)" || v_cf_email=""
        v_cf_key="$(config_get CF_API_KEY 2>/dev/null)" || v_cf_key=""
        local v_rules
        v_rules="$(curl -sf -X GET "https://api.cloudflare.com/client/v4/zones/${v_cf_zone}/rulesets/phases/http_request_cache_settings/entrypoint" \
            -H "X-Auth-Email: ${v_cf_email}" -H "X-Auth-Key: ${v_cf_key}" 2>/dev/null)" || v_rules=""
        if echo "$v_rules" | grep -q "$new_domain"; then
            printf "  ${GREEN}OK${NC}  Cloudflare:  cache rule for %s\n" "$new_domain"
        else
            printf "  ${RED}!!${NC}  Cloudflare:  cache rule NOT updated\n"
        fi
    else
        printf "  ${DIM}--${NC}  Cloudflare:  not configured\n"
    fi

    # Preload queue
    local v_preload="${new_web_root}/wp-content/uploads/acms-preload-queue.txt"
    if [[ -f "$v_preload" ]]; then
        printf "  ${YELLOW}--${NC}  Preload:     queue file exists (may have old URLs)\n"
    else
        printf "  ${GREEN}OK${NC}  Preload:     queue cleared\n"
    fi

    printf "  ──────────────────────────────────────\n\n"
}

