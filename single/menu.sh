    local sub="$1" extra="${2:-}"

    case "$sub" in
        # --- SSL ---
        ssl)
            local ssl_sub="${extra}"
            if [[ -z "$ssl_sub" ]]; then
                _header "SSL Management"
                local _last_method
                _last_method="$(state_get SSL_METHOD_USED 2>/dev/null)" || _last_method="-"
