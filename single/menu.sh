# ===========================================================================
# MAIN DISPATCHER
# ===========================================================================
main() {
    load_state

    if [[ $# -gt 0 ]]; then
        case "$1" in
            status)   menu_status ;;
            wp)       menu_wordpress "${2:-}" ;;
            db)       menu_database "${2:-}" "${3:-}" ;;
            cache)    menu_cache "${2:-}" ;;
            backup)   menu_backup "${2:-}" "${3:-}" ;;
            cron)     menu_cron "${2:-}" "${3:-}" ;;
            domain)   menu_domain "${2:-}" ;;
            advanced) menu_advanced "${2:-}" "${3:-}" ;;
            help|-h|--help) menu_help ;;
            # Shortcuts
            login)    _wp_auto_login ;;
            pma)      menu_database pma ;;
            retune)   menu_advanced perf retune ;;
            ssl)      menu_advanced ssl "${2:-}" ;;
            *)        printf "Unknown: %s\n" "$1"; show_usage ;;
        esac
    else
        while true; do
            show_menu
            printf "\n"
            read -rp "  Choose [0-9]: " choice
            case "$choice" in
                1) menu_status ;;
                2) menu_wordpress ;;
                3) menu_database ;;
                4) menu_cache ;;
                5) menu_backup ;;
                6) menu_cron ;;
                7) menu_domain ;;
                8) menu_advanced ;;
                9) menu_help ;;
                0) printf "  Goodbye!\n"; exit 0 ;;
                *) ;;
            esac
        done
    fi
}

main "$@"
