#!/usr/bin/env bash
set -euo pipefail

# ---------------------------------------------------------------------------
# Resolve paths (follow symlink to real location)
# ---------------------------------------------------------------------------
_SELF="${BASH_SOURCE[0]}"
while [[ -L "$_SELF" ]]; do
    _DIR="$(cd "$(dirname "$_SELF")" && pwd)"
    _SELF="$(readlink "$_SELF")"
    [[ "$_SELF" != /* ]] && _SELF="$_DIR/$_SELF"
done
SCRIPT_DIR="$(cd "$(dirname "$_SELF")" && pwd)"
AZ_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
export AZ_DIR

# ---------------------------------------------------------------------------
# Source libraries
# ---------------------------------------------------------------------------
source "$AZ_DIR/lib/common.sh"
az_init

for _lib in detect tuning nginx php mariadb redis wordpress ssl firewall security cron backup; do
    [[ -f "$AZ_DIR/lib/${_lib}.sh" ]] && source "$AZ_DIR/lib/${_lib}.sh"
done

trap 'trap_error $LINENO "$BASH_COMMAND"' ERR

# ---------------------------------------------------------------------------
# Load state
# ---------------------------------------------------------------------------
load_state() {
    [[ ! -f "$AZ_STATE_FILE" ]] && die "az-wp not installed. Run install.sh first."
    DOMAIN="$(state_get DOMAIN)" || die "DOMAIN not found in state."
    PHP_VERSION="$(state_get PHP_VERSION 2>/dev/null)" || PHP_VERSION="8.5"
    SITE_USER="$(state_get SITE_USER 2>/dev/null)"     || SITE_USER=""
    WEB_ROOT="$(state_get WEB_ROOT 2>/dev/null)"       || WEB_ROOT=""
    CACHE_PATH="$(state_get CACHE_PATH 2>/dev/null)"   || CACHE_PATH=""
    REDIS_SOCK="$(state_get REDIS_SOCK 2>/dev/null)"   || REDIS_SOCK="/run/redis/redis-server.sock"
    DB_NAME="$(state_get DB_NAME 2>/dev/null)"         || DB_NAME=""
    DB_USER="$(state_get DB_USER 2>/dev/null)"         || DB_USER=""
    DB_PASS="$(state_get DB_PASS 2>/dev/null)"         || DB_PASS=""
}

# ---------------------------------------------------------------------------
# WP-CLI helper (suppresses PHP 8.5 deprecation noise)
# ---------------------------------------------------------------------------
wp_run() {
    sudo -u "$SITE_USER" wp "$@" --path="$WEB_ROOT" 2>&1 | grep -v "^Deprecated:" || true
}

# ---------------------------------------------------------------------------
# Section header helper
# ---------------------------------------------------------------------------
_header() { printf "\n${BOLD}  %s${NC}\n  ──────────────────────────────────────\n" "$1"; }

# Auto-login: generate one-time token URL for admin user
_wp_auto_login() {
    local token
    token="$(openssl rand -hex 32)"
    local expiry
    expiry="$(date -d '+1 hour' '+%s' 2>/dev/null || date -v+1H '+%s' 2>/dev/null)"

    # Store token in WP option (WP-CLI)
    wp_run option update _az_wp_login_token "${token}:${expiry}" > /dev/null

    # Ensure mu-plugin exists for token processing
    local mu_dir="$WEB_ROOT/wp-content/mu-plugins"
    local mu_file="$mu_dir/az-wp-autologin.php"
    if [[ ! -f "$mu_file" ]]; then
        mkdir -p "$mu_dir"
        cat > "$mu_file" <<'MUPHP'
<?php
/**
 * az-wp Auto-Login via Token
 * Handles one-time login tokens generated by az-wp CLI.
 */
add_action('init', function () {
    if (empty($_GET['az_login_token'])) return;
    $token = sanitize_text_field($_GET['az_login_token']);
    $stored = get_option('_az_wp_login_token', '');
    if (empty($stored)) return;

    list($valid_token, $expiry) = explode(':', $stored, 2);
    if (!hash_equals($valid_token, $token)) return;
    if (time() > (int)$expiry) {
        delete_option('_az_wp_login_token');
        wp_die('Login link expired. Generate a new one: az-wp login');
    }

    // Valid — login as user ID 1 and delete token (one-time use)
    delete_option('_az_wp_login_token');
    wp_set_auth_cookie(1, true);
    wp_set_current_user(1);
    wp_safe_redirect(admin_url());
    exit;
}, 1);
MUPHP
        chown "$SITE_USER:$SITE_USER" "$mu_file"
    fi

    local login_url="https://${DOMAIN}/?az_login_token=${token}"
    printf "\n  ${BOLD}Auto-Login URL:${NC}\n"
    printf "  %s\n\n" "$login_url"
    printf "  ${DIM}One-time use. Expires in 1 hour.${NC}\n"
    printf "  ${DIM}Generate new: az-wp login${NC}\n\n"
}

# Generate/refresh cache-stats.json with real file count and size
_refresh_cache_stats() {
    local stats_dir="/home/${SITE_USER}/cache"
    local stats_file="${stats_dir}/cache-stats.json"
    local cache_dir="${CACHE_PATH:-${stats_dir}/fastcgi}"
    mkdir -p "$stats_dir"
    local count size
    count="$(find "$cache_dir" -type f 2>/dev/null | wc -l || echo 0)"
    size="$(du -sb "$cache_dir" 2>/dev/null | awk '{print $1}' || echo 0)"
    printf '{"files":%s,"size":%s,"updated":"%s"}' "$count" "$size" "$(date '+%Y-%m-%d %H:%M:%S')" > "$stats_file"
    chown "${SITE_USER}:${SITE_USER}" "$stats_file" 2>/dev/null || true
}


