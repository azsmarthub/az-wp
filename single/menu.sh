                fi
            fi

            printf "  ${DIM}Command:${NC} %s\n\n" "$cmd"
            printf "  ${BOLD}Output:${NC}\n"
            eval "$cmd" 2>&1 | head -30 | sed 's/^/  /'
            local exit_code=$?
            printf "\n  ${DIM}Exit code: %d${NC}\n" "$exit_code"
